    'autorouting','on');
end


function code = finalControllerCode()
% 代码作用：
% 返回写入MATLAB Function模块的完整最终控制器代码。
% 注意：输出尺寸由Stateflow数据属性固定，不使用coder.varsize。

code = sprintf([ ...
'function [qDotCmd,centerError,depthError,rcondJc,rcondArho,qDotCenter,qDotDepthRaw,qDotDepthWeighted,qDotNullUsed] = ...\n' ...
'    fcn(q,cLMeasured,rhoHatL,vHatLeft,JL,cLd,rhoD,validLeft,ekfPredictionValid,centerEnable,depthEnable,armEnable,depthTaskWeight, ...\n' ...
'    cfg_numericalEpsilon,cfg_robustEnable,cfg_betaC,cfg_epsilonC,cfg_Kc,cfg_lambdaC,cfg_betaRho,cfg_epsilonRho,cfg_kRho,cfg_lambdaRho,cfg_nullspaceEnable,cfg_kNull,cfg_qMid,cfg_leftOnlyCenterControlEnable)\n' ...
'%%#codegen\n' ...
'%% 代码作用：\n' ...
'%% 左相机归一化中心为主任务，逆深度为次任务。\n' ...
'%% 支持目标速度前馈、鲁棒项、阻尼伪逆和关节中位零空间任务。\n' ...
'\n' ...
'qDotCmd = zeros(7,1);\n' ...
'centerError = zeros(2,1);\n' ...
'depthError = 0;\n' ...
'rcondJc = 0;\n' ...
'rcondArho = 0;\n' ...
'qDotCenter = zeros(7,1);\n' ...
'qDotDepthRaw = zeros(7,1);\n' ...
'qDotDepthWeighted = zeros(7,1);\n' ...
'qDotNullUsed = zeros(7,1);\n' ...
'\n' ...
'x = cLMeasured(1);\n' ...
'y = cLMeasured(2);\n' ...
'rho = rhoD;\n' ...
'vFeed = zeros(3,1);\n' ...
'\n' ...
'if ekfPredictionValid > 0.5 && ...\n' ...
'        isfinite(rhoHatL) && rhoHatL > 0 && ...\n' ...
'        all(isfinite(vHatLeft))\n' ...
'    rho = max(rhoHatL,cfg_numericalEpsilon);\n' ...
'    vFeed = vHatLeft;\n' ...
'end\n' ...
'\n' ...
'centerError = [x;y] - cLd;\n' ...
'depthError = rho - rhoD;\n' ...
'\n' ...
'Lc = [\n' ...
'    -rho, 0, x*rho, x*y, -(1+x*x), y\n' ...
'    0, -rho, y*rho, 1+y*y, -x*y, -x\n' ...
'];\n' ...
'\n' ...
'Hc = rho*[1 0 -x;0 1 -y];\n' ...
'bhatC = Hc*vFeed;\n' ...
'Jc = Lc*JL;\n' ...
'\n' ...
'rc = zeros(2,1);\n' ...
'if cfg_robustEnable > 0.5\n' ...
'    rc = cfg_betaC*centerError / ...\n' ...
'        (sqrt(centerError''*centerError)+cfg_epsilonC);\n' ...
'end\n' ...
'\n' ...
'nuC = -cfg_Kc*centerError - bhatC - rc;\n' ...
'Gc = Jc*Jc'' + cfg_lambdaC^2*eye(2);\n' ...
'JcSharp = Jc''/Gc;\n' ...
'qC = JcSharp*nuC;\n' ...
'Nc = eye(7)-JcSharp*Jc;\n' ...
'rcondJc = det(Gc) / ...\n' ...
'    (trace(Gc)*trace(Gc)+cfg_numericalEpsilon);\n' ...
'qDotCenter = qC;\n' ...
'\n' ...
'Lrho = [0 0 rho*rho rho*y -rho*x 0];\n' ...
'Hrho = [0 0 -rho*rho];\n' ...
'Jrho = Lrho*JL;\n' ...
'bhatRho = Hrho*vFeed;\n' ...
'\n' ...
'rr = 0;\n' ...
'if cfg_robustEnable > 0.5\n' ...
'    rr = cfg_betaRho*depthError / ...\n' ...
'        (abs(depthError)+cfg_epsilonRho);\n' ...
'end\n' ...
'\n' ...
'nuRho = -cfg_kRho*depthError-bhatRho-rr;\n' ...
'A = Jrho*Nc;\n' ...
'denom = A*A''+cfg_lambdaRho^2;\n' ...
'rcondArho = denom/(1+denom);\n' ...
'\n' ...
'w = min(max(depthTaskWeight,0),1) * ...\n' ...
'    double(ekfPredictionValid > 0.5);\n' ...
'qDotDepthRaw = Nc*A''*((nuRho-Jrho*qC)/denom);\n' ...
'qDotDepthWeighted = w*qDotDepthRaw;\n' ...
'\n' ...
'qDot0 = zeros(7,1);\n' ...
'if cfg_nullspaceEnable > 0.5\n' ...
'    qDot0 = -cfg_kNull*(q-cfg_qMid);\n' ...
'end\n' ...
'\n' ...
'ArhoDagger = A''/denom;\n' ...
'NcRho = Nc*(eye(7)-ArhoDagger*A);\n' ...
'Nnull = (1-w)*Nc+w*NcRho;\n' ...
'qDotNullUsed = Nnull*qDot0;\n' ...
'\n' ...
'if centerEnable > 0.5\n' ...
'    qDotCmd = qDotCenter+qDotNullUsed;\n' ...
'end\n' ...
'\n' ...
'if centerEnable > 0.5 && depthEnable > 0.5\n' ...
'    qDotCmd = qDotCenter+qDotDepthWeighted+qDotNullUsed;\n' ...
'end\n' ...
'\n' ...
'if centerEnable <= 0.5 && depthEnable > 0.5\n' ...
'    d = Jrho*Jrho''+cfg_lambdaRho^2;\n' ...
'    qDotDepthRaw = Jrho''*(nuRho/d);\n' ...
'    qDotDepthWeighted = w*qDotDepthRaw;\n' ...
'    qDotCenter = zeros(7,1);\n' ...
'    qDotNullUsed = zeros(7,1);\n' ...
'    qDotCmd = qDotDepthWeighted;\n' ...
'end\n' ...
'\n' ...
'centerAllowed = validLeft > 0.5 && ...\n' ...
'    cfg_leftOnlyCenterControlEnable > 0.5;\n' ...
'\n' ...
'if ~centerAllowed || armEnable <= 0.5\n' ...
'    qDotCmd = zeros(7,1);\n' ...
'    qDotCenter = zeros(7,1);\n' ...
'    qDotDepthWeighted = zeros(7,1);\n' ...
'    qDotNullUsed = zeros(7,1);\n' ...
'end\n' ...
'\n' ...
'if any(~isfinite(qDotCmd))\n' ...
'    qDotCmd = zeros(7,1);\n' ...
'end\n' ...
'end\n']);
end
