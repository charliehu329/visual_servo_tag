function build_arm_priority_controller_final(coreModelFile)
% BUILD_ARM_PRIORITY_CONTROLLER_FINAL
% 代码作用：
% 自动重建 stereo_ibvs_core/07_Arm_Priority_Controller 子系统内部。
% 本脚本只修改该子系统内部，不连接 Core 顶层信号。
%
% 输入参数含义：
% coreModelFile：
% 可选，stereo_ibvs_core.slx 的完整路径。省略时，脚本按照
% visual_servo_tag/simulink/build/core/ 的标准目录结构自动查找。
%
% 输出参数含义：
% 本函数没有输出参数。
%
% 重要说明：
% 1. 运行前会备份整个 stereo_ibvs_core.slx；
% 2. 会删除 07_Arm_Priority_Controller 当前内部全部模块和连线；
% 3. 不修改 Core 顶层连线，因此运行后暂时不要执行整模型仿真；
% 4. 下一步需要继续连接 07 子系统新增的顶层输入输出。

%% 1. 定位工程文件
scriptDir = fileparts(mfilename('fullpath'));

if nargin < 1 || isempty(coreModelFile)
    % 标准位置：
    % visual_servo_tag/simulink/build/core/build_arm_priority_controller_final.m
    buildDir = fileparts(scriptDir);
    simulinkDir = fileparts(buildDir);
    coreModelFile = fullfile( ...
        simulinkDir, ...
        'core', ...
        'stereo_ibvs_core.slx');
else
    coreModelFile = char(coreModelFile);
    coreDir = fileparts(coreModelFile);
    simulinkDir = fileparts(coreDir);
end

configFile = fullfile( ...
    simulinkDir, ...
    'config', ...
    'stereo_ibvs_config.m');

if ~isfile(coreModelFile)
    error('FinalController:CoreModelNotFound', ...
        '找不到Core模型：%s', coreModelFile);
end

if ~isfile(configFile)
    error('FinalController:ConfigNotFound', ...
        '找不到配置文件：%s', configFile);
end

[coreDir, modelName] = fileparts(coreModelFile);
addpath(coreDir);
addpath(fileparts(configFile));

%% 2. 加载配置，并临时补齐最终控制器参数
run(configFile);

if ~exist('cfg','var') || ~isstruct(cfg)
    error('FinalController:CfgMissing', ...
        '运行配置文件后没有生成cfg结构体。');
end

[cfg, addedFields] = addMissingControllerConfigFields(cfg);
assignin('base','cfg',cfg);

if ~isempty(addedFields)
    fprintf('\n以下参数当前配置文件中不存在，已仅在本次MATLAB会话临时补齐：\n');
    for fieldIndex = 1:numel(addedFields)
        fprintf('  cfg.%s\n', addedFields{fieldIndex});
    end
    fprintf('后续需要把这些参数正式写入stereo_ibvs_config.m。\n\n');
end

%% 3. 加载模型并建立备份
wasLoaded = bdIsLoaded(modelName);
if ~wasLoaded
    load_system(coreModelFile);
end

backupDir = fullfile(coreDir,'backup');
if ~isfolder(backupDir)
    mkdir(backupDir);
end

backupFile = fullfile( ...
    backupDir, ...
    sprintf('%s_before_final_controller_%s.slx', ...
    modelName, datestr(now,'yyyymmdd_HHMMSS')));

% 先保存当前内存状态，再复制出独立备份文件。
save_system(modelName, coreModelFile);
copyfile(coreModelFile, backupFile);
fprintf('已备份Core模型：\n%s\n', backupFile);

subsystemPath = [modelName '/07_Arm_Priority_Controller'];
if getSimulinkBlockHandle(subsystemPath) == -1
    error('FinalController:SubsystemNotFound', ...
        '找不到子系统：%s', subsystemPath);
end

%% 4. 清空07子系统内部
Simulink.SubSystem.deleteContents(subsystemPath);

%% 5. 创建13个子系统输入端口
inputNames = {
    'q'
    'cLMeasured'
    'rhoHatL'
    'vHatLeft'
    'JL'
    'validLeft'
    'ekfPredictionValid'
    'cameraModelValid'
    'kinematicsValid'
    'controllerEnable'
    'centerEnable'
    'depthEnable'
    'depthTaskWeight'
};

inputDimensions = {
    '[7 1]'
    '[2 1]'
    '1'
    '[3 1]'
    '[6 7]'
    '1'
    '1'
    '1'
    '1'
    '1'
    '1'
    '1'
    '1'
};

for inputIndex = 1:numel(inputNames)
    y = 35 + (inputIndex-1)*42;
    blockPath = [subsystemPath '/' inputNames{inputIndex}];

    add_block( ...
        'simulink/Ports & Subsystems/In1', ...
        blockPath, ...
        'Port', num2str(inputIndex), ...
        'PortDimensions', inputDimensions{inputIndex}, ...
        'Position', [25 y 55 y+18]);
end

%% 6. 创建机械臂总使能门
% 使用乘法而不是Logical Operator，保证输出保持double类型。
armEnableGatePath = [subsystemPath '/Arm Enable Gate'];
add_block( ...
    'simulink/Math Operations/Product', ...
    armEnableGatePath, ...
    'Inputs', '***', ...
    'Position', [235 340 275 410]);

add_line(subsystemPath, ...
    'cameraModelValid/1', ...
    'Arm Enable Gate/1', ...
    'autorouting','on');
add_line(subsystemPath, ...
    'kinematicsValid/1', ...
    'Arm Enable Gate/2', ...
    'autorouting','on');
add_line(subsystemPath, ...
    'controllerEnable/1', ...
    'Arm Enable Gate/3', ...
    'autorouting','on');

%% 7. 创建配置Constant模块
constantSpecs = {
    'P_centerDesired',                 'cfg.centerDesired'
    'P_rhoD',                          'cfg.rhoD'
    'P_numericalEpsilon',              'cfg.numericalEpsilon'
    'P_robustEnable',                  'double(cfg.robustEnable)'
    'P_betaC',                         'cfg.betaC'
    'P_epsilonC',                      'cfg.epsilonC'
    'P_Kc',                            'cfg.Kc'
    'P_lambdaC',                       'cfg.lambdaC'
    'P_betaRho',                       'cfg.betaRho'
    'P_epsilonRho',                    'cfg.epsilonRho'
    'P_kRho',                          'cfg.kRho'
    'P_lambdaRho',                     'cfg.lambdaRho'
    'P_nullspaceEnable',               'double(cfg.nullspaceEnable)'
    'P_kNull',                         'cfg.kNull'
    'P_qMid',                          'cfg.qMid'
    'P_leftOnlyCenterControlEnable',   'double(cfg.leftOnlyCenterControlEnable)'
};

for constantIndex = 1:size(constantSpecs,1)
    columnIndex = floor((constantIndex-1)/8);
    rowIndex = mod(constantIndex-1,8);

    x = 80 + columnIndex*230;
    y = 620 + rowIndex*52;

    blockPath = [subsystemPath '/' constantSpecs{constantIndex,1}];
    add_block( ...
        'simulink/Sources/Constant', ...
        blockPath, ...
        'Value', constantSpecs{constantIndex,2}, ...
        'SampleTime', '-1', ...
        'VectorParams1D', 'off', ...
        'Position', [x y x+185 y+28]);
end

%% 8. 创建MATLAB Function模块并写入完整控制器代码
functionBlockName = 'Final Arm Priority Controller';
functionBlockPath = [subsystemPath '/' functionBlockName];

add_block( ...
    'simulink/User-Defined Functions/MATLAB Function', ...
    functionBlockPath, ...
    'Position', [520 70 790 870]);

chart = find( ...
    sfroot, ...
    '-isa', 'Stateflow.EMChart', ...
    'Path', functionBlockPath);

if numel(chart) ~= 1
    error('FinalController:ChartNotFound', ...
        '无法唯一找到MATLAB Function图表：%s', functionBlockPath);
end

chart.Script = finalControllerCode();
chart.SupportVariableSizing = false;
drawnow;

%% 9. 显式固定MATLAB Function全部输入输出维度
chartDataSpecs = {
    'q',                               '[7 1]'
    'cLMeasured',                      '[2 1]'
    'rhoHatL',                         '[1 1]'
    'vHatLeft',                        '[3 1]'
    'JL',                              '[6 7]'
    'cLd',                             '[2 1]'
    'rhoD',                            '[1 1]'
    'validLeft',                       '[1 1]'
    'ekfPredictionValid',              '[1 1]'
    'centerEnable',                    '[1 1]'
    'depthEnable',                     '[1 1]'
    'armEnable',                       '[1 1]'
    'depthTaskWeight',                 '[1 1]'
    'cfg_numericalEpsilon',            '[1 1]'
    'cfg_robustEnable',                '[1 1]'
    'cfg_betaC',                       '[1 1]'
    'cfg_epsilonC',                    '[1 1]'
    'cfg_Kc',                          '[2 2]'
    'cfg_lambdaC',                     '[1 1]'
    'cfg_betaRho',                     '[1 1]'
    'cfg_epsilonRho',                  '[1 1]'
    'cfg_kRho',                        '[1 1]'
    'cfg_lambdaRho',                   '[1 1]'
    'cfg_nullspaceEnable',             '[1 1]'
    'cfg_kNull',                       '[1 1]'
    'cfg_qMid',                        '[7 1]'
    'cfg_leftOnlyCenterControlEnable', '[1 1]'
    'qDotCmd',                         '[7 1]'
    'centerError',                     '[2 1]'
    'depthError',                      '[1 1]'
    'rcondJc',                         '[1 1]'
    'rcondArho',                       '[1 1]'
    'qDotCenter',                      '[7 1]'
    'qDotDepthRaw',                    '[7 1]'
    'qDotDepthWeighted',               '[7 1]'
    'qDotNullUsed',                    '[7 1]'
};

for dataIndex = 1:size(chartDataSpecs,1)
    setChartDataProperties( ...
        chart, ...
        chartDataSpecs{dataIndex,1}, ...
        chartDataSpecs{dataIndex,2});
end

%% 10. 连接子系统输入到MATLAB Function
% Function输入1～5。
connect(subsystemPath,'q',1,functionBlockName,1);
connect(subsystemPath,'cLMeasured',1,functionBlockName,2);
connect(subsystemPath,'rhoHatL',1,functionBlockName,3);
connect(subsystemPath,'vHatLeft',1,functionBlockName,4);
connect(subsystemPath,'JL',1,functionBlockName,5);

% Function输入6～7：期望中心和期望逆深度。
connect(subsystemPath,'P_centerDesired',1,functionBlockName,6);
connect(subsystemPath,'P_rhoD',1,functionBlockName,7);

% Function输入8～13：有效性、任务使能和权重。
connect(subsystemPath,'validLeft',1,functionBlockName,8);
connect(subsystemPath,'ekfPredictionValid',1,functionBlockName,9);
connect(subsystemPath,'centerEnable',1,functionBlockName,10);
connect(subsystemPath,'depthEnable',1,functionBlockName,11);
connect(subsystemPath,'Arm Enable Gate',1,functionBlockName,12);
connect(subsystemPath,'depthTaskWeight',1,functionBlockName,13);

% Function输入14～27：控制器配置参数。
parameterBlockNames = {
    'P_numericalEpsilon'
    'P_robustEnable'
    'P_betaC'
    'P_epsilonC'
    'P_Kc'
    'P_lambdaC'
    'P_betaRho'
    'P_epsilonRho'
    'P_kRho'
    'P_lambdaRho'
    'P_nullspaceEnable'
    'P_kNull'
    'P_qMid'
    'P_leftOnlyCenterControlEnable'
};

for parameterIndex = 1:numel(parameterBlockNames)
    connect( ...
        subsystemPath, ...
        parameterBlockNames{parameterIndex}, ...
        1, ...
        functionBlockName, ...
        13 + parameterIndex);
end

%% 11. 创建并连接9个输出端口
outputNames = {
    'qDotCmd'
    'centerError'
    'depthError'
    'rcondJc'
    'rcondArho'
    'qDotCenter'
    'qDotDepthRaw'
    'qDotDepthWeighted'
    'qDotNullUsed'
};

outputDimensions = {
    '[7 1]'
    '[2 1]'
    '1'
    '1'
    '1'
    '[7 1]'
    '[7 1]'
    '[7 1]'
    '[7 1]'
};

for outputIndex = 1:numel(outputNames)
    y = 110 + (outputIndex-1)*78;
    blockPath = [subsystemPath '/' outputNames{outputIndex}];

    add_block( ...
        'simulink/Ports & Subsystems/Out1', ...
        blockPath, ...
        'Port', num2str(outputIndex), ...
        'PortDimensions', outputDimensions{outputIndex}, ...
        'Position', [1040 y 1070 y+18]);

    connect( ...
        subsystemPath, ...
        functionBlockName, ...
        outputIndex, ...
        outputNames{outputIndex}, ...
        1);
end

%% 12. 添加说明并保存
annotationText = sprintf([ ...
    'Final Arm Priority Controller\n' ...
    'Primary: left normalized center\n' ...
    'Secondary: inverse depth in primary null space\n' ...
    'Output dimensions are explicitly fixed; do not use coder.varsize here.']);

annotation = Simulink.Annotation(subsystemPath, annotationText);
annotation.Position = [825 30 1120 90];

save_system(modelName, coreModelFile);

fprintf('\n07_Arm_Priority_Controller内部构建完成。\n');
fprintf('模型已保存：\n%s\n', coreModelFile);
fprintf(['当前只完成子系统内部。下一步必须连接Core顶层新增端口，' ...
    '然后才能执行整模型Update Diagram。\n']);

if ~wasLoaded
    open_system(modelName);
end
open_system(subsystemPath);
end


function [cfg, addedFields] = addMissingControllerConfigFields(cfg)
% 代码作用：
% 仅在当前MATLAB会话中补齐原模型控制器依赖的配置参数。

addedFields = {};

[cfg, addedFields] = addField(cfg,addedFields, ...
    'numericalEpsilon',1e-12);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'robustEnable',0);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'betaC',0);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'epsilonC',1e-3);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'betaRho',0);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'epsilonRho',1e-3);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'kRho',1.5);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'lambdaRho',0.02);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'nullspaceEnable',0);
[cfg, addedFields] = addField(cfg,addedFields, ...
    'kNull',0.05);

if ~isfield(cfg,'qMid')
    if ~isfield(cfg,'qMin') || ~isfield(cfg,'qMax')
        error('FinalController:JointLimitsMissing', ...
            'cfg缺少qMin/qMax，无法计算qMid。');
    end
    cfg.qMid = 0.5*(cfg.qMin + cfg.qMax);
    addedFields{end+1} = 'qMid'; %#ok<AGROW>
end

[cfg, addedFields] = addField(cfg,addedFields, ...
    'leftOnlyCenterControlEnable',1);

requiredExistingFields = {
    'centerDesired'
    'rhoD'
    'Kc'
    'lambdaC'
};

for fieldIndex = 1:numel(requiredExistingFields)
    if ~isfield(cfg,requiredExistingFields{fieldIndex})
        error('FinalController:RequiredCfgMissing', ...
            'cfg缺少必要参数：cfg.%s', ...
            requiredExistingFields{fieldIndex});
    end
end
end


function [cfg, addedFields] = addField( ...
    cfg, addedFields, fieldName, defaultValue)
% 代码作用：
% 缺少字段时加入默认值，并记录字段名称。

if ~isfield(cfg,fieldName)
    cfg.(fieldName) = defaultValue;
    addedFields{end+1} = fieldName; %#ok<AGROW>
end
end


function setChartDataProperties(chart,dataName,arraySize)
% 代码作用：
% 显式设置MATLAB Function输入输出数据类型和固定尺寸。

allData = chart.find( ...
    '-isa', 'Stateflow.Data', ...
    'Name', dataName);

if isempty(allData)
    error('FinalController:ChartDataMissing', ...
        'MATLAB Function中找不到数据：%s', dataName);
end

% 同名数据正常情况下只有一个；若Stateflow返回多个，只设置当前Chart直属数据。
data = allData(1);
data.DataType = 'double';
data.Props.Array.Size = arraySize;
end


