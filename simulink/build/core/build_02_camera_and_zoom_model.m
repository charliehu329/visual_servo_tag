function build_02_camera_and_zoom_model(modelName)
%BUILD_02_CAMERA_AND_ZOOM_MODEL Rebuild Core subsystem 02.
%
% Usage:
%   stereo_ibvs_config
%   open_system('stereo_ibvs_core')
%   build_02_camera_and_zoom_model('stereo_ibvs_core')
%
% This script rebuilds only the inside of subsystem 02.
% It does not connect subsystem 02 to the Core top level.
% All cfg values are supplied by Constant blocks, not MATLAB Function
% parameters. All created blocks use a white background.

if nargin < 1 || strlength(string(modelName)) == 0
    modelName = 'stereo_ibvs_core';
end
modelName = char(modelName);

load_system('simulink');
if ~bdIsLoaded(modelName)
    load_system(modelName);
end

requiredCfgFields = { ...
    'focalLengthHardwareMinMm', ...
    'focalLengthHardwareMaxMm', ...
    'focalLengthWorkingMinMm', ...
    'focalLengthWorkingMaxMm', ...
    'outputPixelPitchXmm', ...
    'outputPixelPitchYmm', ...
    'focalLengthTimeoutFrames', ...
    'numericalEpsilon'};

validateCfgFields(requiredCfgFields);

subsystemPath = locateSubsystem02(modelName);
clearSubsystemContents(subsystemPath);

set_param(subsystemPath, ...
    'BackgroundColor', 'white', ...
    'ForegroundColor', 'black');

%% External signal inputs
inputNames = { ...
    'focalLengthMeasuredMmRaw', ...
    'focalLengthIsNew', ...
    'resetSafe'};

inputDimensions = {'2','1','1'};

inputY = [70 145 220];

for inputIndex = 1:numel(inputNames)
    y = inputY(inputIndex);
    blockPath = [subsystemPath '/' inputNames{inputIndex}];

    add_block('simulink/Ports & Subsystems/In1', blockPath, ...
        'Port', num2str(inputIndex), ...
        'PortDimensions', inputDimensions{inputIndex}, ...
        'BackgroundColor', 'white', ...
        'ForegroundColor', 'black', ...
        'Position', [30 y 145 y+24]);
end

%% Configuration Constants
constantNames = { ...
    'HardwareMinMm', ...
    'HardwareMaxMm', ...
    'WorkingMinMm', ...
    'WorkingMaxMm', ...
    'PixelPitchXmm', ...
    'PixelPitchYmm', ...
    'TimeoutFrames', ...
    'NumericalEpsilon'};

constantValues = { ...
    'cfg.focalLengthHardwareMinMm', ...
    'cfg.focalLengthHardwareMaxMm', ...
    'cfg.focalLengthWorkingMinMm', ...
    'cfg.focalLengthWorkingMaxMm', ...
    'cfg.outputPixelPitchXmm', ...
    'cfg.outputPixelPitchYmm', ...
    'cfg.focalLengthTimeoutFrames', ...
    'cfg.numericalEpsilon'};

constantY = 310:65:(310 + 65*(numel(constantNames)-1));

for constantIndex = 1:numel(constantNames)
    y = constantY(constantIndex);
    blockPath = [subsystemPath '/' constantNames{constantIndex}];

    add_block('simulink/Sources/Constant', blockPath, ...
        'Value', constantValues{constantIndex}, ...
        'BackgroundColor', 'white', ...
        'ForegroundColor', 'black', ...
        'Position', [45 y 205 y+30]);
end

%% MATLAB Function
functionBlockName = 'V2_Camera_and_Zoom_Model';
functionBlockPath = [subsystemPath '/' functionBlockName];

add_block('simulink/User-Defined Functions/MATLAB Function', ...
    functionBlockPath, ...
    'BackgroundColor', 'white', ...
    'ForegroundColor', 'black', ...
    'Position', [390 45 735 850]);

functionCode = strjoin({ ...
    'function [ ...', ...
    '    focalLengthMeasuredMm, ...', ...
    '    fxMeasuredPx, ...', ...
    '    fyMeasuredPx, ...', ...
    '    focalLengthFresh, ...', ...
    '    focalLengthDataValid, ...', ...
    '    focalAtWorkingLowerLimit, ...', ...
    '    focalAtWorkingUpperLimit, ...', ...
    '    focalAtHardwareLowerLimit, ...', ...
    '    focalAtHardwareUpperLimit, ...', ...
    '    focalOutsideWorkingRange, ...', ...
    '    focalHardwareLimitViolation, ...', ...
    '    focalRangeUsage, ...', ...
    '    focalHeadroomMm, ...', ...
    '    cameraModelValid] = fcn( ...', ...
    '    focalLengthMeasuredMmRaw, ...', ...
    '    focalLengthIsNew, ...', ...
    '    resetSafe, ...', ...
    '    cfg_focalLengthHardwareMinMm, ...', ...
    '    cfg_focalLengthHardwareMaxMm, ...', ...
    '    cfg_focalLengthWorkingMinMm, ...', ...
    '    cfg_focalLengthWorkingMaxMm, ...', ...
    '    cfg_outputPixelPitchXmm, ...', ...
    '    cfg_outputPixelPitchYmm, ...', ...
    '    cfg_focalLengthTimeoutFrames, ...', ...
    '    cfg_numericalEpsilon)', ...
    '%#codegen', ...
    '% V2 Camera and Zoom Model', ...
    '%', ...
    '% 焦距反馈：', ...
    '% [fL_mm; fR_mm]', ...
    '%', ...
    '% 超时后保持最后一次有效焦距，但 focalLengthFresh = false。', ...
    '% 08模块应在 focalLengthFresh = false 时停止Zoom命令。', ...
    '', ...
    '%% 固定尺寸输入', ...
    'focalRaw = reshape(focalLengthMeasuredMmRaw,2,1);', ...
    '', ...
    'hardwareMin = reshape( ...', ...
    '    cfg_focalLengthHardwareMinMm,2,1);', ...
    '', ...
    'hardwareMax = reshape( ...', ...
    '    cfg_focalLengthHardwareMaxMm,2,1);', ...
    '', ...
    'workingMin = reshape( ...', ...
    '    cfg_focalLengthWorkingMinMm,2,1);', ...
    '', ...
    'workingMax = reshape( ...', ...
    '    cfg_focalLengthWorkingMaxMm,2,1);', ...
    '', ...
    '%% 持久状态', ...
    'persistent lastValidFocalLengthMm', ...
    'persistent focalLengthReceivedOnce', ...
    'persistent framesSinceLastValidMessage', ...
    '', ...
    'if isempty(lastValidFocalLengthMm)', ...
    '    lastValidFocalLengthMm = zeros(2,1);', ...
    '    focalLengthReceivedOnce = false;', ...
    '    framesSinceLastValidMessage = ...', ...
    '        double(cfg_focalLengthTimeoutFrames);', ...
    'end', ...
    '', ...
    '%% 固定尺寸输出', ...
    'focalLengthMeasuredMm = zeros(2,1);', ...
    'fxMeasuredPx = zeros(2,1);', ...
    'fyMeasuredPx = zeros(2,1);', ...
    '', ...
    'focalLengthFresh = false;', ...
    'focalLengthDataValid = false;', ...
    '', ...
    'focalAtWorkingLowerLimit = false(2,1);', ...
    'focalAtWorkingUpperLimit = false(2,1);', ...
    'focalAtHardwareLowerLimit = false(2,1);', ...
    'focalAtHardwareUpperLimit = false(2,1);', ...
    '', ...
    'focalOutsideWorkingRange = false(2,1);', ...
    'focalHardwareLimitViolation = false(2,1);', ...
    '', ...
    'focalRangeUsage = zeros(2,1);', ...
    'focalHeadroomMm = zeros(2,1);', ...
    '', ...
    'cameraModelValid = false;', ...
    '', ...
    '%% 配置检查', ...
    'numericalConfigurationValid = ...', ...
    '    isfinite(cfg_numericalEpsilon) && ...', ...
    '    cfg_numericalEpsilon > 0;', ...
    '', ...
    'hardwareRangeValid = ...', ...
    '    all(isfinite(hardwareMin)) && ...', ...
    '    all(isfinite(hardwareMax)) && ...', ...
    '    all(hardwareMax > hardwareMin);', ...
    '', ...
    'workingRangeValid = ...', ...
    '    all(isfinite(workingMin)) && ...', ...
    '    all(isfinite(workingMax)) && ...', ...
    '    all(workingMax > workingMin) && ...', ...
    '    all(workingMin >= hardwareMin) && ...', ...
    '    all(workingMax <= hardwareMax);', ...
    '', ...
    'pixelPitchValid = ...', ...
    '    isfinite(cfg_outputPixelPitchXmm) && ...', ...
    '    isfinite(cfg_outputPixelPitchYmm) && ...', ...
    '    cfg_outputPixelPitchXmm > cfg_numericalEpsilon && ...', ...
    '    cfg_outputPixelPitchYmm > cfg_numericalEpsilon;', ...
    '', ...
    'timeoutConfigurationValid = ...', ...
    '    isfinite(cfg_focalLengthTimeoutFrames) && ...', ...
    '    cfg_focalLengthTimeoutFrames >= 1;', ...
    '', ...
    'configurationValid = ...', ...
    '    numericalConfigurationValid && ...', ...
    '    hardwareRangeValid && ...', ...
    '    workingRangeValid && ...', ...
    '    pixelPitchValid && ...', ...
    '    timeoutConfigurationValid;', ...
    '', ...
    '%% 控制输入检查', ...
    'controlSignalsFinite = ...', ...
    '    isfinite(focalLengthIsNew) && ...', ...
    '    isfinite(resetSafe);', ...
    '', ...
    '%% 复位或更新焦距状态', ...
    'if ~controlSignalsFinite || resetSafe > 0.5', ...
    '', ...
    '    lastValidFocalLengthMm = zeros(2,1);', ...
    '    focalLengthReceivedOnce = false;', ...
    '    framesSinceLastValidMessage = ...', ...
    '        double(cfg_focalLengthTimeoutFrames);', ...
    '', ...
    'else', ...
    '', ...
    '    rawFinite = all(isfinite(focalRaw));', ...
    '', ...
    '    rawInsideHardwareRange = ...', ...
    '        rawFinite && ...', ...
    '        hardwareRangeValid && ...', ...
    '        all(focalRaw >= hardwareMin) && ...', ...
    '        all(focalRaw <= hardwareMax);', ...
    '', ...
    '    newValidMessage = ...', ...
    '        focalLengthIsNew > 0.5 && ...', ...
    '        rawInsideHardwareRange && ...', ...
    '        configurationValid;', ...
    '', ...
    '    if newValidMessage', ...
    '', ...
    '        lastValidFocalLengthMm = focalRaw;', ...
    '        focalLengthReceivedOnce = true;', ...
    '        framesSinceLastValidMessage = 0;', ...
    '', ...
    '    elseif focalLengthReceivedOnce', ...
    '', ...
    '        maximumCounterValue = ...', ...
    '            double(cfg_focalLengthTimeoutFrames) + 1;', ...
    '', ...
    '        framesSinceLastValidMessage = min( ...', ...
    '            framesSinceLastValidMessage + 1, ...', ...
    '            maximumCounterValue);', ...
    '    end', ...
    'end', ...
    '', ...
    '%% 输出最近一次有效焦距', ...
    'if focalLengthReceivedOnce', ...
    '    focalLengthMeasuredMm = ...', ...
    '        lastValidFocalLengthMm;', ...
    'end', ...
    '', ...
    '%% 焦距新鲜度', ...
    'focalLengthFresh = ...', ...
    '    focalLengthReceivedOnce && ...', ...
    '    framesSinceLastValidMessage < ...', ...
    '        double(cfg_focalLengthTimeoutFrames);', ...
    '', ...
    '%% 保存焦距有效性', ...
    'if focalLengthReceivedOnce && hardwareRangeValid', ...
    '', ...
    '    focalLengthDataValid = ...', ...
    '        all(isfinite(focalLengthMeasuredMm)) && ...', ...
    '        all(focalLengthMeasuredMm >= hardwareMin) && ...', ...
    '        all(focalLengthMeasuredMm <= hardwareMax);', ...
    'end', ...
    '', ...
    '%% 焦距范围状态', ...
    'if focalLengthReceivedOnce && configurationValid', ...
    '', ...
    '    focalAtWorkingLowerLimit = ...', ...
    '        focalLengthMeasuredMm <= workingMin;', ...
    '', ...
    '    focalAtWorkingUpperLimit = ...', ...
    '        focalLengthMeasuredMm >= workingMax;', ...
    '', ...
    '    focalAtHardwareLowerLimit = ...', ...
    '        focalLengthMeasuredMm <= hardwareMin;', ...
    '', ...
    '    focalAtHardwareUpperLimit = ...', ...
    '        focalLengthMeasuredMm >= hardwareMax;', ...
    '', ...
    '    focalOutsideWorkingRange = ...', ...
    '        focalLengthMeasuredMm < workingMin | ...', ...
    '        focalLengthMeasuredMm > workingMax;', ...
    '', ...
    '    focalHardwareLimitViolation = ...', ...
    '        focalLengthMeasuredMm < hardwareMin | ...', ...
    '        focalLengthMeasuredMm > hardwareMax;', ...
    '', ...
    '    workingRangeWidth = ...', ...
    '        workingMax - workingMin;', ...
    '', ...
    '    focalRangeUsage = ...', ...
    '        (focalLengthMeasuredMm-workingMin) ./ ...', ...
    '        workingRangeWidth;', ...
    '', ...
    '    focalRangeUsage = min( ...', ...
    '        max(focalRangeUsage,zeros(2,1)), ...', ...
    '        ones(2,1));', ...
    '', ...
    '    lowerHeadroomMm = ...', ...
    '        focalLengthMeasuredMm-workingMin;', ...
    '', ...
    '    upperHeadroomMm = ...', ...
    '        workingMax-focalLengthMeasuredMm;', ...
    '', ...
    '    focalHeadroomMm = max( ...', ...
    '        min(lowerHeadroomMm,upperHeadroomMm), ...', ...
    '        zeros(2,1));', ...
    'end', ...
    '', ...
    '%% 焦距mm转换为像素焦距', ...
    'cameraModelValid = ...', ...
    '    configurationValid && ...', ...
    '    focalLengthDataValid && ...', ...
    '    ~any(focalHardwareLimitViolation);', ...
    '', ...
    'if cameraModelValid', ...
    '', ...
    '    fxMeasuredPx = ...', ...
    '        focalLengthMeasuredMm ./ ...', ...
    '        cfg_outputPixelPitchXmm;', ...
    '', ...
    '    fyMeasuredPx = ...', ...
    '        focalLengthMeasuredMm ./ ...', ...
    '        cfg_outputPixelPitchYmm;', ...
    'end', ...
    '', ...
    '%% 最终有限值保护', ...
    'outputFinite = ...', ...
    '    all(isfinite(focalLengthMeasuredMm)) && ...', ...
    '    all(isfinite(fxMeasuredPx)) && ...', ...
    '    all(isfinite(fyMeasuredPx)) && ...', ...
    '    all(isfinite(focalRangeUsage)) && ...', ...
    '    all(isfinite(focalHeadroomMm));', ...
    '', ...
    'if ~outputFinite', ...
    '', ...
    '    focalLengthMeasuredMm = zeros(2,1);', ...
    '    fxMeasuredPx = zeros(2,1);', ...
    '    fyMeasuredPx = zeros(2,1);', ...
    '', ...
    '    focalLengthFresh = false;', ...
    '    focalLengthDataValid = false;', ...
    '', ...
    '    focalAtWorkingLowerLimit = false(2,1);', ...
    '    focalAtWorkingUpperLimit = false(2,1);', ...
    '    focalAtHardwareLowerLimit = false(2,1);', ...
    '    focalAtHardwareUpperLimit = false(2,1);', ...
    '', ...
    '    focalOutsideWorkingRange = false(2,1);', ...
    '    focalHardwareLimitViolation = false(2,1);', ...
    '', ...
    '    focalRangeUsage = zeros(2,1);', ...
    '    focalHeadroomMm = zeros(2,1);', ...
    '', ...
    '    cameraModelValid = false;', ...
    'end', ...
    'end'}, newline);

stateflowRoot = sfroot;
chart = stateflowRoot.find( ...
    '-isa', 'Stateflow.EMChart', ...
    'Path', functionBlockPath);

if isempty(chart)
    error('StereoIBVS:MATLABFunctionChartNotFound', ...
        '无法找到 MATLAB Function 图表：%s', functionBlockPath);
end

chart = chart(1);
chart.Script = functionCode;

%% Outputs
outputNames = { ...
    'focalLengthMeasuredMm', ...
    'fxMeasuredPx', ...
    'fyMeasuredPx', ...
    'focalLengthFresh', ...
    'focalLengthDataValid', ...
    'focalAtWorkingLowerLimit', ...
    'focalAtWorkingUpperLimit', ...
    'focalAtHardwareLowerLimit', ...
    'focalAtHardwareUpperLimit', ...
    'focalOutsideWorkingRange', ...
    'focalHardwareLimitViolation', ...
    'focalRangeUsage', ...
    'focalHeadroomMm', ...
    'cameraModelValid'};

outputDimensions = { ...
    '2','2','2','1','1','2','2','2','2','2','2','2','2','1'};

outputSpacing = 56;
outputTop = 55;

for outputIndex = 1:numel(outputNames)
    y = outputTop + (outputIndex-1)*outputSpacing;
    blockPath = [subsystemPath '/' outputNames{outputIndex}];

    add_block('simulink/Ports & Subsystems/Out1', blockPath, ...
        'Port', num2str(outputIndex), ...
        'PortDimensions', outputDimensions{outputIndex}, ...
        'BackgroundColor', 'white', ...
        'ForegroundColor', 'black', ...
        'Position', [850 y 1030 y+24]);
end

%% Internal connections
for inputIndex = 1:numel(inputNames)
    add_line(subsystemPath, ...
        [inputNames{inputIndex} '/1'], ...
        [functionBlockName '/' num2str(inputIndex)], ...
        'autorouting', 'on');
end

for constantIndex = 1:numel(constantNames)
    destinationPort = numel(inputNames) + constantIndex;

    add_line(subsystemPath, ...
        [constantNames{constantIndex} '/1'], ...
        [functionBlockName '/' num2str(destinationPort)], ...
        'autorouting', 'on');
end

for outputIndex = 1:numel(outputNames)
    add_line(subsystemPath, ...
        [functionBlockName '/' num2str(outputIndex)], ...
        [outputNames{outputIndex} '/1'], ...
        'autorouting', 'on');
end

Simulink.BlockDiagram.arrangeSystem(subsystemPath);
set_param(subsystemPath, 'ZoomFactor', 'FitSystem');

save_system(modelName);

fprintf('\n02 Camera and Zoom Model 已重建。\n');
fprintf('子系统：%s\n', subsystemPath);
fprintf('外部输入：3\n');
fprintf('Constant：8\n');
fprintf('输出：14\n');
fprintf('未连接 Core 顶层线路。\n');
end


function subsystemPath = locateSubsystem02(modelName)
candidateNames = { ...
    '02_Camera_and_Zoom_Model', ...
    '02_Camera_Model', ...
    '02_Camera Zoom Model', ...
    '02 Camera and Zoom Model'};

for candidateIndex = 1:numel(candidateNames)
    candidatePath = [modelName '/' candidateNames{candidateIndex}];

    if getSimulinkBlockHandle(candidatePath) ~= -1
        subsystemPath = candidatePath;
        return;
    end
end

topSubsystems = find_system( ...
    modelName, ...
    'SearchDepth', 1, ...
    'BlockType', 'SubSystem');

matchedSubsystems = {};

for blockIndex = 1:numel(topSubsystems)
    [~, blockName] = fileparts(topSubsystems{blockIndex});
    blockNameLower = lower(blockName);

    if startsWith(blockNameLower, '02') && ...
            contains(blockNameLower, 'camera')
        matchedSubsystems{end+1} = topSubsystems{blockIndex}; %#ok<AGROW>
    end
end

if numel(matchedSubsystems) ~= 1
    error('StereoIBVS:Subsystem02NotFound', ...
        ['无法唯一定位02子系统。请将其命名为' ...
         '02_Camera_and_Zoom_Model。']);
end

subsystemPath = matchedSubsystems{1};
end


function clearSubsystemContents(subsystemPath)
lineHandles = find_system( ...
    subsystemPath, ...
    'FindAll', 'on', ...
    'SearchDepth', 1, ...
    'Type', 'line');

for lineIndex = 1:numel(lineHandles)
    if ishandle(lineHandles(lineIndex))
        delete_line(lineHandles(lineIndex));
    end
end

blocks = find_system( ...
    subsystemPath, ...
    'SearchDepth', 1, ...
    'Type', 'Block');

blocks(strcmp(blocks, subsystemPath)) = [];

for blockIndex = 1:numel(blocks)
    if getSimulinkBlockHandle(blocks{blockIndex}) ~= -1
        delete_block(blocks{blockIndex});
    end
end
end


function validateCfgFields(requiredFields)
if evalin('base', 'exist(''cfg'',''var'')') ~= 1
    error('StereoIBVS:CfgMissing', ...
        '基础工作区中没有 cfg，请先运行 stereo_ibvs_config。');
end

cfgValue = evalin('base', 'cfg');

for fieldIndex = 1:numel(requiredFields)
    fieldName = requiredFields{fieldIndex};

    if ~isfield(cfgValue, fieldName)
        error('StereoIBVS:CfgFieldMissing', ...
            'cfg 缺少字段：%s', fieldName);
    end
end
end
