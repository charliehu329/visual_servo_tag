function build_06_estimated_rho_dynamics(modelName)
%BUILD_06_ESTIMATED_RHO_DYNAMICS Rebuild the V2 rho dynamics subsystem.
%
% Usage:
%   build_06_estimated_rho_dynamics
%   build_06_estimated_rho_dynamics('stereo_ibvs_core')
%
% The builder:
%   1. Keeps the Core top-level subsystem block.
%   2. Clears and rebuilds only the subsystem contents.
%   3. Builds signal Inports, one cfg Constant, MATLAB Function and Outports.
%   4. Does not connect or modify Core top-level lines.

if nargin < 1 || strlength(string(modelName)) == 0
    modelName = 'stereo_ibvs_core';
end

modelName = char(erase(string(modelName), '.slx'));

if ~bdIsLoaded(modelName)
    load_system(modelName);
end

%% Locate the existing 06 subsystem without creating a duplicate.
preferredName = '06_Estimated_Rho_Dynamics';
legacyName = '06_Inverse_Depth_Filter';

preferredPath = [modelName '/' preferredName];
legacyPath = [modelName '/' legacyName];

if getSimulinkBlockHandle(preferredPath) ~= -1
    subsystemName = preferredName;
    subsystemPath = preferredPath;
elseif getSimulinkBlockHandle(legacyPath) ~= -1
    subsystemName = legacyName;
    subsystemPath = legacyPath;
else
    subsystemName = preferredName;
    subsystemPath = preferredPath;

    add_block( ...
        'simulink/Ports & Subsystems/Subsystem', ...
        subsystemPath, ...
        'Position', [1080 250 1360 590]);
end

% Keep the top-level subsystem block and replace only its internal content.
Simulink.SubSystem.deleteContents(subsystemPath);
set_param(subsystemPath, 'TreatAsAtomicUnit', 'on');

%% External signal inputs
inputNames = { ...
    'normalizedFeatureMeasured', ...
    'rhoHatSafe', ...
    'vHatCL', ...
    'vHatCR', ...
    'JL', ...
    'JR', ...
    'qDotAppliedDelayed'};

inputDimensions = { ...
    '[4 1]', ...
    '1', ...
    '[3 1]', ...
    '[3 1]', ...
    '[6 7]', ...
    '[6 7]', ...
    '[7 1]'};

for inputIndex = 1:numel(inputNames)
    y = 55 + (inputIndex - 1) * 82;
    blockPath = [subsystemPath '/' inputNames{inputIndex}];

    add_block( ...
        'simulink/Sources/In1', ...
        blockPath, ...
        'Port', num2str(inputIndex), ...
        'OutDataTypeStr', 'double', ...
        'PortDimensions', inputDimensions{inputIndex}, ...
        'Position', [30 y 235 y+22]);
end

%% Configuration Constant
constantName = 'numericalEpsilon';
constantPath = [subsystemPath '/' constantName];

add_block( ...
    'simulink/Sources/Constant', ...
    constantPath, ...
    'Value', 'cfg.numericalEpsilon', ...
    'OutDataTypeStr', 'double', ...
    'SampleTime', '-1', ...
    'Position', [275 650 485 678]);

%% MATLAB Function
functionBlockName = 'Estimate Rho Dynamics';
functionBlockPath = [subsystemPath '/' functionBlockName];

add_block( ...
    'simulink/User-Defined Functions/MATLAB Function', ...
    functionBlockPath, ...
    'Position', [570 45 980 700]);

stateflowRoot = sfroot;
chart = find( ...
    stateflowRoot, ...
    '-isa', 'Stateflow.EMChart', ...
    'Path', functionBlockPath);

if isempty(chart)
    error( ...
        'StereoIBVS:RhoDynamicsChartNotFound', ...
        '无法找到 MATLAB Function Chart：%s', ...
        functionBlockPath);
end

chart.Script = estimatedRhoDynamicsCode();
drawnow;

%% Outputs
outputNames = { ...
    'rhoDotHat', ...
    'rhoDynamicsValid'};

outputDimensions = { ...
    '[2 1]', ...
    '1'};

for outputIndex = 1:numel(outputNames)
    y = 145 + (outputIndex - 1) * 240;
    blockPath = [subsystemPath '/' outputNames{outputIndex}];

    add_block( ...
        'simulink/Sinks/Out1', ...
        blockPath, ...
        'Port', num2str(outputIndex), ...
        'PortDimensions', outputDimensions{outputIndex}, ...
        'Position', [1135 y 1355 y+22]);
end

%% Internal connections
% Signal inputs -> MATLAB Function inputs 1...7.
for inputIndex = 1:numel(inputNames)
    addNamedLine( ...
        subsystemPath, ...
        [inputNames{inputIndex} '/1'], ...
        [functionBlockName '/' num2str(inputIndex)], ...
        inputNames{inputIndex});
end

% Constant -> MATLAB Function input 8.
addNamedLine( ...
    subsystemPath, ...
    [constantName '/1'], ...
    [functionBlockName '/8'], ...
    'cfg_numericalEpsilon');

% MATLAB Function outputs -> subsystem outputs.
for outputIndex = 1:numel(outputNames)
    addNamedLine( ...
        subsystemPath, ...
        [functionBlockName '/' num2str(outputIndex)], ...
        [outputNames{outputIndex} '/1'], ...
        outputNames{outputIndex});
end

%% Interface verification
functionPorts = get_param(functionBlockPath, 'PortHandles');

if numel(functionPorts.Inport) ~= 8
    error( ...
        'StereoIBVS:RhoDynamicsInputCount', ...
        'MATLAB Function 应有8个输入，实际为%d个。', ...
        numel(functionPorts.Inport));
end

if numel(functionPorts.Outport) ~= 2
    error( ...
        'StereoIBVS:RhoDynamicsOutputCount', ...
        'MATLAB Function 应有2个输出，实际为%d个。', ...
        numel(functionPorts.Outport));
end

save_system(modelName);

fprintf('\n%s 构建完成。\n', subsystemName);
fprintf('输入：7个信号 + 1个 Constant。\n');
fprintf('输出：2个。\n');
fprintf('未修改 Core 顶层连线。\n');
end


function addNamedLine(systemPath, sourcePort, destinationPort, signalName)
lineHandle = add_line( ...
    systemPath, ...
    sourcePort, ...
    destinationPort, ...
    'autorouting', 'on');

if ~isempty(signalName)
    try
        set_param(lineHandle, 'Name', signalName);
    catch
        % Line naming is cosmetic; a routing branch must not stop the build.
    end
end
end


function code = estimatedRhoDynamicsCode()
codeLines = { ...
'function [rhoDotHat, rhoDynamicsValid] = fcn( ...'; ...
'    normalizedFeatureMeasured, ...'; ...
'    rhoHatSafe, ...'; ...
'    vHatCL, ...'; ...
'    vHatCR, ...'; ...
'    JL, ...'; ...
'    JR, ...'; ...
'    qDotAppliedDelayed, cfg_numericalEpsilon)'; ...
'%#codegen'; ...
'% V2 Estimated Rho Dynamics'; ...
'%'; ...
'% 输入尺寸：'; ...
'% normalizedFeatureMeasured : 4×1 [xL;yL;xR;yR]'; ...
'% rhoHatSafe                : 1×1'; ...
'% vHatCL                    : 3×1'; ...
'% vHatCR                    : 3×1'; ...
'% JL                        : 6×7'; ...
'% JR                        : 6×7'; ...
'% qDotAppliedDelayed        : 7×1'; ...
'%'; ...
'% 输出：'; ...
'% rhoDotHat                 : 2×1'; ...
'% rhoDynamicsValid          : 1×1'; ...
''; ...
'%% 固定尺寸输出'; ...
'rhoDotHat = zeros(2,1);'; ...
'rhoDynamicsValid = false;'; ...
''; ...
'features = reshape(normalizedFeatureMeasured,4,1);'; ...
'velocityCL = reshape(vHatCL,3,1);'; ...
'velocityCR = reshape(vHatCR,3,1);'; ...
'qDotDelayed = reshape(qDotAppliedDelayed,7,1);'; ...
''; ...
'%% 输入有效性'; ...
'inputValid = ...'; ...
'    all(isfinite(features)) && ...'; ...
'    isfinite(rhoHatSafe) && ...'; ...
'    all(isfinite(velocityCL)) && ...'; ...
'    all(isfinite(velocityCR)) && ...'; ...
'    all(isfinite(JL(:))) && ...'; ...
'    all(isfinite(JR(:))) && ...'; ...
'    all(isfinite(qDotDelayed)) && ...'; ...
'    isfinite(cfg_numericalEpsilon) && ...'; ...
'    cfg_numericalEpsilon > 0 && ...'; ...
'    rhoHatSafe > cfg_numericalEpsilon;'; ...
''; ...
'if ~inputValid'; ...
'    return;'; ...
'end'; ...
''; ...
'rho = max(rhoHatSafe, cfg_numericalEpsilon);'; ...
''; ...
'%% 左相机'; ...
'xL = features(1);'; ...
'yL = features(2);'; ...
''; ...
'LrhoL = ['; ...
'    0, 0, rho*rho, ...'; ...
'    rho*yL, -rho*xL, 0'; ...
'];'; ...
''; ...
'Hrho = [0, 0, -rho*rho];'; ...
''; ...
'cameraVelocityLeft = JL * qDotDelayed;'; ...
''; ...
'rhoDotHat(1) = ...'; ...
'    LrhoL * cameraVelocityLeft + ...'; ...
'    Hrho * velocityCL;'; ...
''; ...
'%% 右相机'; ...
'xR = features(3);'; ...
'yR = features(4);'; ...
''; ...
'LrhoR = ['; ...
'    0, 0, rho*rho, ...'; ...
'    rho*yR, -rho*xR, 0'; ...
'];'; ...
''; ...
'cameraVelocityRight = JR * qDotDelayed;'; ...
''; ...
'rhoDotHat(2) = ...'; ...
'    LrhoR * cameraVelocityRight + ...'; ...
'    Hrho * velocityCR;'; ...
''; ...
'%% 最终数值保护'; ...
'if all(isfinite(rhoDotHat))'; ...
'    rhoDynamicsValid = true;'; ...
'else'; ...
'    rhoDotHat = zeros(2,1);'; ...
'    rhoDynamicsValid = false;'; ...
'end'; ...
'end'};

code = strjoin(codeLines, newline);
end
