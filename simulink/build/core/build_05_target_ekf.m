function build_05_target_ekf()
%BUILD_05_TARGET_EKF
% 重建 stereo_ibvs_core/05_Target_EKF。
%
% 主要修改：
% 1. 增加 visionMeasurementNewSafe 输入；
% 2. 同一条视觉消息只允许执行一次测量更新；
% 3. 支持首次有效双目测量初始化世界坐标目标位置；
% 4. reset 后重新等待首次有效测量；
% 5. 所有 cfg_* 使用 Constant 块；
% 6. 不连接 Core 顶层线路，背景为白色。

%% 路径
buildDir = fileparts(mfilename('fullpath'));
simulinkDir = fileparts(fileparts(buildDir));
modelFile = fullfile(simulinkDir, 'core', 'stereo_ibvs_core.slx');

if ~isfile(modelFile)
    error('StereoIBVS:CoreModelNotFound', ...
        '找不到 Core 模型：%s', modelFile);
end

[~, modelName] = fileparts(modelFile);
load_system(modelFile);

subsystemPath = [modelName '/05_Target_EKF'];

%% 删除旧子系统
if getSimulinkBlockHandle(subsystemPath) ~= -1
    delete_block(subsystemPath);
end

add_block( ...
    'simulink/Ports & Subsystems/Subsystem', ...
    subsystemPath, ...
    'Position', [1220 90 1560 820], ...
    'BackgroundColor', 'white');

deleteIfExists([subsystemPath '/In1']);
deleteIfExists([subsystemPath '/Out1']);

%% 外部输入
inputDefinitions = {
    'zMeasured',                    1, '[3 1]'
    'measurementEnable',            2, '1'
    'visionMeasurementNewSafe',     3, '1'
    'pWCL',                         4, '[3 1]'
    'RWCL',                         5, '[3 3]'
    'fxMeasuredPx',                 6, '[2 1]'
    'fyMeasuredPx',                 7, '[2 1]'
    'resetSafe',                    8, '1'
};

inputY = 45:50:(45 + 50*(size(inputDefinitions,1)-1));

for inputIndex = 1:size(inputDefinitions,1)
    addInport( ...
        subsystemPath, ...
        inputDefinitions{inputIndex,1}, ...
        inputDefinitions{inputIndex,2}, ...
        inputDefinitions{inputIndex,3}, ...
        [25 inputY(inputIndex) 55 inputY(inputIndex)+14]);
end

%% 配置 Constant
constantNames = {
    'EKF Initial State'
    'EKF Initial Covariance'
    'Sample Time'
    'Jerk Standard Deviation'
    'Minimum Visible Depth'
    'Numerical Epsilon'
    'Minimum Rho'
    'Maximum Rho'
    'Pixel Noise Standard Deviation'
    'Stereo Baseline'
    'Covariance Jitter'
    'Innovation Condition Minimum'
    'EKF Initialization Mode'
    'Require New Measurement'
};

constantValues = {
    'cfg_ekfX0'
    'cfg_ekfP0'
    'cfg_Ts'
    'cfg_sigmaJerk'
    'cfg_visibilityZMin'
    'cfg_numericalEpsilon'
    'cfg_rhoMin'
    'cfg_rhoMax'
    'cfg_pixelNoiseStd'
    'cfg_baseline'
    'cfg_ekfCovarianceJitter'
    'cfg_ekfSConditionMin'
    'cfg_ekfInitializationMode'
    'cfg_ekfRequireNewMeasurement'
};

constantY = 455:42:(455 + 42*(numel(constantNames)-1));

for constantIndex = 1:numel(constantNames)
    add_block( ...
        'simulink/Sources/Constant', ...
        [subsystemPath '/' constantNames{constantIndex}], ...
        'Value', constantValues{constantIndex}, ...
        'OutDataTypeStr', 'double', ...
        'SampleTime', '-1', ...
        'Position', ...
        [20 constantY(constantIndex) 150 constantY(constantIndex)+24]);
end

%% MATLAB Function
functionBlockPath = [subsystemPath '/V2 Target EKF'];

add_block( ...
    'simulink/User-Defined Functions/MATLAB Function', ...
    functionBlockPath, ...
    'Position', [220 35 625 1020]);

chart = find( ...
    sfroot, ...
    '-isa', 'Stateflow.EMChart', ...
    'Path', functionBlockPath);

if isempty(chart)
    error('StereoIBVS:MATLABFunctionNotFound', ...
        '无法找到 MATLAB Function：%s', functionBlockPath);
end

chart.Script = targetEkfCode();

%% 输出端口
outputDefinitions = {
    'pHatW',                  1,  '[3 1]'
    'vHatW',                  2,  '[3 1]'
    'aHatW',                  3,  '[3 1]'
    'pHatLeft',               4,  '[3 1]'
    'vHatLeft',               5,  '[3 1]'
    'rhoHatRaw',              6,  '1'
    'rhoHatSafe',             7,  '1'
    'innovation',             8,  '[3 1]'
    'rhoInnovation',          9,  '1'
    'innovationNorm',        10,  '1'
    'traceP',                11,  '1'
    'ekfPredictionValid',    12,  '1'
    'ekfMeasurementUpdated', 13,  '1'
    'ekfStateFinite',        14,  '1'
    'minEigenvalueP',        15,  '1'
    'symmetryErrorP',        16,  '1'
};

outputY = 45:55:(45 + 55*(size(outputDefinitions,1)-1));

for outputIndex = 1:size(outputDefinitions,1)
    addOutport( ...
        subsystemPath, ...
        outputDefinitions{outputIndex,1}, ...
        outputDefinitions{outputIndex,2}, ...
        outputDefinitions{outputIndex,3}, ...
        [730 outputY(outputIndex) 760 outputY(outputIndex)+14]);
end

%% 内部输入连线
for inputIndex = 1:size(inputDefinitions,1)
    add_line( ...
        subsystemPath, ...
        [inputDefinitions{inputIndex,1} '/1'], ...
        sprintf('V2 Target EKF/%d', inputIndex), ...
        'autorouting', 'on');
end

for constantIndex = 1:numel(constantNames)
    add_line( ...
        subsystemPath, ...
        [constantNames{constantIndex} '/1'], ...
        sprintf('V2 Target EKF/%d', ...
            size(inputDefinitions,1) + constantIndex), ...
        'autorouting', 'on');
end

%% 内部输出连线
for outputIndex = 1:size(outputDefinitions,1)
    add_line( ...
        subsystemPath, ...
        sprintf('V2 Target EKF/%d', outputIndex), ...
        [outputDefinitions{outputIndex,1} '/1'], ...
        'autorouting', 'on');
end

%% 保存
save_system(modelName);

fprintf('\n05_Target_EKF 重建完成。\n');
fprintf('模型：%s\n', modelFile);
fprintf('增加输入：visionMeasurementNewSafe。\n');
fprintf('初始化：首次有效双目测量初始化目标世界坐标位置。\n');
fprintf('注意：本脚本没有连接 Core 顶层线路。\n');
end


function addInport(parentPath, blockName, portNumber, dimensions, position)
add_block( ...
    'simulink/Sources/In1', ...
    [parentPath '/' blockName], ...
    'Port', num2str(portNumber), ...
    'PortDimensions', dimensions, ...
    'OutDataTypeStr', 'double', ...
    'Position', position);
end


function addOutport(parentPath, blockName, portNumber, dimensions, position)
add_block( ...
    'simulink/Sinks/Out1', ...
    [parentPath '/' blockName], ...
    'Port', num2str(portNumber), ...
    'PortDimensions', dimensions, ...
    'Position', position);
end


function deleteIfExists(blockPath)
if getSimulinkBlockHandle(blockPath) ~= -1
    delete_block(blockPath);
end
end


function code = targetEkfCode()
code = sprintf([ ...
'function [ ...\n' ...
'    pHatW, ...\n' ...
'    vHatW, ...\n' ...
'    aHatW, ...\n' ...
'    pHatLeft, ...\n' ...
'    vHatLeft, ...\n' ...
'    rhoHatRaw, ...\n' ...
'    rhoHatSafe, ...\n' ...
'    innovation, ...\n' ...
'    rhoInnovation, ...\n' ...
'    innovationNorm, ...\n' ...
'    traceP, ...\n' ...
'    ekfPredictionValid, ...\n' ...
'    ekfMeasurementUpdated, ...\n' ...
'    ekfStateFinite, ...\n' ...
'    minEigenvalueP, ...\n' ...
'    symmetryErrorP] = fcn( ...\n' ...
'    zMeasured, ...\n' ...
'    measurementEnable, ...\n' ...
'    visionMeasurementNewSafe, ...\n' ...
'    pWCL, ...\n' ...
'    RWCL, ...\n' ...
'    fxMeasuredPx, ...\n' ...
'    fyMeasuredPx, ...\n' ...
'    resetSafe, ...\n' ...
'    cfg_ekfX0, ...\n' ...
'    cfg_ekfP0, ...\n' ...
'    cfg_Ts, ...\n' ...
'    cfg_sigmaJerk, ...\n' ...
'    cfg_visibilityZMin, ...\n' ...
'    cfg_numericalEpsilon, ...\n' ...
'    cfg_rhoMin, ...\n' ...
'    cfg_rhoMax, ...\n' ...
'    cfg_pixelNoiseStd, ...\n' ...
'    cfg_baseline, ...\n' ...
'    cfg_ekfCovarianceJitter, ...\n' ...
'    cfg_ekfSConditionMin, ...\n' ...
'    cfg_ekfInitializationMode, ...\n' ...
'    cfg_ekfRequireNewMeasurement)\n' ...
'%%#codegen\n' ...
'%% V2九状态世界坐标恒加速度EKF。\n' ...
'%% 首次有效双目测量可直接初始化目标世界坐标位置。\n' ...
'\n' ...
'%% 固定尺寸输出\n' ...
'pHatW = zeros(3,1);\n' ...
'vHatW = zeros(3,1);\n' ...
'aHatW = zeros(3,1);\n' ...
'pHatLeft = zeros(3,1);\n' ...
'vHatLeft = zeros(3,1);\n' ...
'rhoHatRaw = 0;\n' ...
'rhoHatSafe = 0;\n' ...
'innovation = zeros(3,1);\n' ...
'rhoInnovation = 0;\n' ...
'innovationNorm = 0;\n' ...
'traceP = 0;\n' ...
'ekfPredictionValid = 0;\n' ...
'ekfMeasurementUpdated = 0;\n' ...
'ekfStateFinite = 0;\n' ...
'minEigenvalueP = 0;\n' ...
'symmetryErrorP = 0;\n' ...
'\n' ...
'%% 输入整理\n' ...
'z = reshape(zMeasured,3,1);\n' ...
'pCameraW = reshape(pWCL,3,1);\n' ...
'rotationCameraToWorld = reshape(RWCL,3,3);\n' ...
'fx = reshape(fxMeasuredPx,2,1);\n' ...
'fy = reshape(fyMeasuredPx,2,1);\n' ...
'ekfX0 = reshape(cfg_ekfX0,9,1);\n' ...
'ekfP0 = reshape(cfg_ekfP0,9,9);\n' ...
'\n' ...
'%% 持久状态\n' ...
'persistent x P initialized\n' ...
'\n' ...
'if isempty(x)\n' ...
'    x = zeros(9,1);\n' ...
'    P = eye(9);\n' ...
'    initialized = false;\n' ...
'end\n' ...
'\n' ...
'%% 配置和输入有效性\n' ...
'configurationValid = ...\n' ...
'    all(isfinite(ekfX0)) && ...\n' ...
'    all(isfinite(ekfP0(:))) && ...\n' ...
'    isfinite(cfg_Ts) && cfg_Ts > 0 && ...\n' ...
'    isfinite(cfg_sigmaJerk) && cfg_sigmaJerk >= 0 && ...\n' ...
'    isfinite(cfg_visibilityZMin) && cfg_visibilityZMin > 0 && ...\n' ...
'    isfinite(cfg_numericalEpsilon) && cfg_numericalEpsilon > 0 && ...\n' ...
'    isfinite(cfg_rhoMin) && ...\n' ...
'    isfinite(cfg_rhoMax) && ...\n' ...
'    cfg_rhoMax > cfg_rhoMin && ...\n' ...
'    isfinite(cfg_pixelNoiseStd) && cfg_pixelNoiseStd >= 0 && ...\n' ...
'    isfinite(cfg_baseline) && ...\n' ...
'    cfg_baseline > cfg_numericalEpsilon && ...\n' ...
'    isfinite(cfg_ekfCovarianceJitter) && ...\n' ...
'    cfg_ekfCovarianceJitter >= 0 && ...\n' ...
'    isfinite(cfg_ekfSConditionMin) && ...\n' ...
'    cfg_ekfSConditionMin >= 0 && ...\n' ...
'    isfinite(cfg_ekfInitializationMode) && ...\n' ...
'    isfinite(cfg_ekfRequireNewMeasurement);\n' ...
'\n' ...
'kinematicInputFinite = ...\n' ...
'    all(isfinite(pCameraW)) && ...\n' ...
'    all(isfinite(rotationCameraToWorld(:)));\n' ...
'\n' ...
'focalPixelsValid = ...\n' ...
'    all(isfinite(fx)) && ...\n' ...
'    all(isfinite(fy)) && ...\n' ...
'    all(fx > cfg_numericalEpsilon) && ...\n' ...
'    all(fy > cfg_numericalEpsilon);\n' ...
'\n' ...
'measurementEnableValid = ...\n' ...
'    isfinite(measurementEnable) && ...\n' ...
'    measurementEnable > 0.5;\n' ...
'\n' ...
'visionNewValid = ...\n' ...
'    isfinite(visionMeasurementNewSafe) && ...\n' ...
'    visionMeasurementNewSafe > 0.5;\n' ...
'\n' ...
'newMeasurementAccepted = ...\n' ...
'    cfg_ekfRequireNewMeasurement <= 0.5 || ...\n' ...
'    visionNewValid;\n' ...
'\n' ...
'measurementFinite = all(isfinite(z));\n' ...
'measurementRhoValid = ...\n' ...
'    measurementFinite && ...\n' ...
'    z(3) >= cfg_rhoMin && ...\n' ...
'    z(3) <= cfg_rhoMax;\n' ...
'\n' ...
'measurementAvailable = ...\n' ...
'    configurationValid && ...\n' ...
'    measurementEnableValid && ...\n' ...
'    newMeasurementAccepted && ...\n' ...
'    kinematicInputFinite && ...\n' ...
'    focalPixelsValid && ...\n' ...
'    measurementRhoValid;\n' ...
'\n' ...
'resetRequested = ...\n' ...
'    isfinite(resetSafe) && ...\n' ...
'    resetSafe > 0.5;\n' ...
'\n' ...
'if ~configurationValid\n' ...
'    initialized = false;\n' ...
'    return;\n' ...
'end\n' ...
'\n' ...
'if resetRequested\n' ...
'    x = ekfX0;\n' ...
'    P = ekfP0;\n' ...
'    initialized = false;\n' ...
'    return;\n' ...
'end\n' ...
'\n' ...
'%% 固定初值模式兼容\n' ...
'fixedInitializationMode = ...\n' ...
'    cfg_ekfInitializationMode <= 0.5;\n' ...
'\n' ...
'if ~initialized && fixedInitializationMode\n' ...
'    x = ekfX0;\n' ...
'    P = ekfP0;\n' ...
'    initialized = true;\n' ...
'end\n' ...
'\n' ...
'%% 首次有效双目测量初始化\n' ...
'justInitialized = false;\n' ...
'\n' ...
'if ~initialized && measurementAvailable\n' ...
'    rhoInitial = z(3);\n' ...
'    depthInitial = 1/rhoInitial;\n' ...
'\n' ...
'    pInitialLeft = [ ...\n' ...
'        z(1)*depthInitial\n' ...
'        z(2)*depthInitial\n' ...
'        depthInitial\n' ...
'    ];\n' ...
'\n' ...
'    pInitialW = ...\n' ...
'        pCameraW + ...\n' ...
'        rotationCameraToWorld*pInitialLeft;\n' ...
'\n' ...
'    initializationFinite = ...\n' ...
'        all(isfinite(pInitialLeft)) && ...\n' ...
'        all(isfinite(pInitialW)) && ...\n' ...
'        depthInitial > cfg_visibilityZMin;\n' ...
'\n' ...
'    if initializationFinite\n' ...
'        x = [ ...\n' ...
'            pInitialW\n' ...
'            zeros(3,1)\n' ...
'            zeros(3,1)\n' ...
'        ];\n' ...
'        P = ekfP0;\n' ...
'        initialized = true;\n' ...
'        justInitialized = true;\n' ...
'        ekfMeasurementUpdated = 1;\n' ...
'    end\n' ...
'end\n' ...
'\n' ...
'%% 尚未初始化时保持安全无效输出\n' ...
'if ~initialized\n' ...
'    traceP = trace(0.5*(P+P''));\n' ...
'    return;\n' ...
'end\n' ...
'\n' ...
'%% 初始化当周期不重复执行预测和测量更新\n' ...
'if justInitialized\n' ...
'    pHatW = x(1:3);\n' ...
'    vHatW = x(4:6);\n' ...
'    aHatW = x(7:9);\n' ...
'    pHatLeft = ...\n' ...
'        rotationCameraToWorld'' * ...\n' ...
'        (pHatW-pCameraW);\n' ...
'    vHatLeft = rotationCameraToWorld''*vHatW;\n' ...
'\n' ...
'    ekfStateFinite = double( ...\n' ...
'        all(isfinite(x)) && ...\n' ...
'        all(isfinite(P(:))) && ...\n' ...
'        all(isfinite(pHatLeft)) && ...\n' ...
'        all(isfinite(vHatLeft)));\n' ...
'\n' ...
'    if ekfStateFinite > 0.5 && ...\n' ...
'            pHatLeft(3) > cfg_visibilityZMin\n' ...
'        rhoHatRaw = 1/pHatLeft(3);\n' ...
'        rhoHatSafe = min( ...\n' ...
'            max(rhoHatRaw,cfg_rhoMin), ...\n' ...
'            cfg_rhoMax);\n' ...
'        ekfPredictionValid = 1;\n' ...
'    end\n' ...
'\n' ...
'    Psym = 0.5*(P+P'');\n' ...
'    traceP = trace(Psym);\n' ...
'    symmetryErrorP = norm(P-P'',''fro'');\n' ...
'    eigenvaluesP = eig(Psym);\n' ...
'    minEigenvalueP = min(real(eigenvaluesP));\n' ...
'    return;\n' ...
'end\n' ...
'\n' ...
'%% 恒加速度状态模型\n' ...
'I3 = eye(3);\n' ...
'Z3 = zeros(3,3);\n' ...
'I9 = eye(9);\n' ...
'Ts = cfg_Ts;\n' ...
'\n' ...
'F = [ ...\n' ...
'    I3, Ts*I3, 0.5*Ts*Ts*I3\n' ...
'    Z3, I3,    Ts*I3\n' ...
'    Z3, Z3,    I3\n' ...
'];\n' ...
'\n' ...
'sigmaJerkSquared = cfg_sigmaJerk*cfg_sigmaJerk;\n' ...
'\n' ...
'Q = sigmaJerkSquared * [ ...\n' ...
'    (Ts^5/20)*I3, (Ts^4/8)*I3, (Ts^3/6)*I3\n' ...
'    (Ts^4/8)*I3,  (Ts^3/3)*I3, (Ts^2/2)*I3\n' ...
'    (Ts^3/6)*I3,  (Ts^2/2)*I3, Ts*I3\n' ...
'];\n' ...
'\n' ...
'%% 预测\n' ...
'xPred = F*x;\n' ...
'PPred = F*P*F'' + Q;\n' ...
'PPred = 0.5*(PPred+PPred'');\n' ...
'\n' ...
'pPredW = xPred(1:3);\n' ...
'pPredLeft = ...\n' ...
'    rotationCameraToWorld'' * ...\n' ...
'    (pPredW-pCameraW);\n' ...
'\n' ...
'predictionFinite = ...\n' ...
'    kinematicInputFinite && ...\n' ...
'    all(isfinite(xPred)) && ...\n' ...
'    all(isfinite(PPred(:))) && ...\n' ...
'    all(isfinite(pPredLeft));\n' ...
'\n' ...
'ekfPredictionValid = double( ...\n' ...
'    predictionFinite && ...\n' ...
'    pPredLeft(3) > cfg_visibilityZMin);\n' ...
'\n' ...
'%% 默认采用预测结果\n' ...
'x = xPred;\n' ...
'P = PPred;\n' ...
'\n' ...
'%% 仅在新视觉消息到来时执行测量更新\n' ...
'measurementOK = ...\n' ...
'    measurementAvailable && ...\n' ...
'    ekfPredictionValid > 0.5;\n' ...
'\n' ...
'if measurementOK\n' ...
'    Z = pPredLeft(3);\n' ...
'    rhoPred = 1/Z;\n' ...
'    xLeftPred = pPredLeft(1)/Z;\n' ...
'    yLeftPred = pPredLeft(2)/Z;\n' ...
'\n' ...
'    h = [xLeftPred; yLeftPred; rhoPred];\n' ...
'\n' ...
'    HLeft = [ ...\n' ...
'        rhoPred, 0,       -xLeftPred*rhoPred\n' ...
'        0,       rhoPred, -yLeftPred*rhoPred\n' ...
'        0,       0,       -rhoPred*rhoPred\n' ...
'    ];\n' ...
'\n' ...
'    HWorld = HLeft*rotationCameraToWorld'';\n' ...
'    H = [HWorld, zeros(3,3), zeros(3,3)];\n' ...
'\n' ...
'    if all(isfinite(h)) && all(isfinite(H(:)))\n' ...
'        innovation = z-h;\n' ...
'        rhoInnovation = innovation(3);\n' ...
'\n' ...
'        pixelVariance = ...\n' ...
'            cfg_pixelNoiseStd*cfg_pixelNoiseStd;\n' ...
'        fxLeft = max(fx(1),cfg_numericalEpsilon);\n' ...
'        fxRight = max(fx(2),cfg_numericalEpsilon);\n' ...
'        fyLeft = max(fy(1),cfg_numericalEpsilon);\n' ...
'        baseline = max(cfg_baseline,cfg_numericalEpsilon);\n' ...
'\n' ...
'        varXLeft = pixelVariance/(fxLeft*fxLeft);\n' ...
'        varYLeft = pixelVariance/(fyLeft*fyLeft);\n' ...
'        covXLeftRho = ...\n' ...
'            pixelVariance/(baseline*fxLeft*fxLeft);\n' ...
'        varRho = pixelVariance/(baseline*baseline) * ...\n' ...
'            (1/(fxLeft*fxLeft)+1/(fxRight*fxRight));\n' ...
'\n' ...
'        R = [ ...\n' ...
'            varXLeft,    0,        covXLeftRho\n' ...
'            0,           varYLeft, 0\n' ...
'            covXLeftRho, 0,        varRho\n' ...
'        ];\n' ...
'\n' ...
'        R = R + cfg_ekfCovarianceJitter*eye(3);\n' ...
'        S = H*PPred*H'' + R;\n' ...
'\n' ...
'        innovationCovarianceValid = ...\n' ...
'            all(isfinite(S(:))) && ...\n' ...
'            rcond(S) > cfg_ekfSConditionMin;\n' ...
'\n' ...
'        if innovationCovarianceValid\n' ...
'            K = (PPred*H'')/S;\n' ...
'            xUpdated = xPred+K*innovation;\n' ...
'            A = I9-K*H;\n' ...
'            PUpdated = A*PPred*A''+K*R*K'';\n' ...
'            PUpdated = 0.5*(PUpdated+PUpdated'');\n' ...
'\n' ...
'            if cfg_ekfCovarianceJitter > 0\n' ...
'                PUpdated = PUpdated + ...\n' ...
'                    cfg_ekfCovarianceJitter*I9;\n' ...
'            end\n' ...
'\n' ...
'            updateFinite = ...\n' ...
'                all(isfinite(xUpdated)) && ...\n' ...
'                all(isfinite(PUpdated(:)));\n' ...
'\n' ...
'            if updateFinite\n' ...
'                x = xUpdated;\n' ...
'                P = PUpdated;\n' ...
'                ekfMeasurementUpdated = 1;\n' ...
'            end\n' ...
'        end\n' ...
'    end\n' ...
'end\n' ...
'\n' ...
'%% 状态输出\n' ...
'pHatW = x(1:3);\n' ...
'vHatW = x(4:6);\n' ...
'aHatW = x(7:9);\n' ...
'pHatLeft = ...\n' ...
'    rotationCameraToWorld'' * ...\n' ...
'    (pHatW-pCameraW);\n' ...
'vHatLeft = rotationCameraToWorld''*vHatW;\n' ...
'\n' ...
'ekfStateFinite = double( ...\n' ...
'    all(isfinite(x)) && ...\n' ...
'    all(isfinite(P(:))) && ...\n' ...
'    all(isfinite(pHatLeft)) && ...\n' ...
'    all(isfinite(vHatLeft)));\n' ...
'\n' ...
'if ekfStateFinite > 0.5 && ...\n' ...
'        pHatLeft(3) > cfg_visibilityZMin\n' ...
'    rhoHatRaw = 1/pHatLeft(3);\n' ...
'    rhoHatSafe = min( ...\n' ...
'        max(rhoHatRaw,cfg_rhoMin), ...\n' ...
'        cfg_rhoMax);\n' ...
'else\n' ...
'    rhoHatRaw = 0;\n' ...
'    rhoHatSafe = 0;\n' ...
'    ekfPredictionValid = 0;\n' ...
'end\n' ...
'\n' ...
'innovationNorm = sqrt(innovation''*innovation);\n' ...
