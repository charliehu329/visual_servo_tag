function build_04_fr3_camera_kinematics()
%BUILD_04_FR3_CAMERA_KINEMATICS
% 重建 stereo_ibvs_core/04_FR3_Camera_Kinematics。
%
% 设计约定：
% 1. 子系统只有 q 一个外部输入；
% 2. 所有 cfg_* 均使用 Constant 块输入 MATLAB Function；
% 3. 保留 V2 原始运动学、左右相机 Jacobian 和诊断输出；
% 4. 不连接 Core 顶层线路；
% 5. 子系统背景使用白色。

%% 路径
buildDir = fileparts(mfilename('fullpath'));
simulinkDir = fileparts(fileparts(buildDir));
modelFile = fullfile(simulinkDir, 'core', 'stereo_ibvs_core.slx');

if ~isfile(modelFile)
    error('StereoIBVS:CoreModelNotFound', ...
        '找不到 Core 模型：%s', modelFile);
end

[~, modelName] = fileparts(modelFile);
load_system(modelFile);

subsystemPath = [modelName '/04_FR3_Camera_Kinematics'];

%% 删除旧子系统并重新创建
if getSimulinkBlockHandle(subsystemPath) ~= -1
    delete_block(subsystemPath);
end

add_block( ...
    'simulink/Ports & Subsystems/Subsystem', ...
    subsystemPath, ...
    'Position', [820 110 1160 720], ...
    'BackgroundColor', 'white');

deleteIfExists([subsystemPath '/In1']);
deleteIfExists([subsystemPath '/Out1']);

%% 外部输入：只有关节角
addInport( ...
    subsystemPath, ...
    'q', ...
    1, ...
    '[7 1]', ...
    [30 55 60 69]);

%% 配置 Constant
constantNames = {
    'World Base Transform'
    'FR3 Origin XYZ'
    'FR3 Origin RPY'
    'FR3 Joint Axis'
    'FR3 Joint8 Origin XYZ'
    'FR3 Joint8 Origin RPY'
    'Link8 To Left Camera'
    'Left To Right Camera'
    'Numerical Epsilon'
};

constantValues = {
    'cfg_T_W_B'
    'cfg_fr3OriginXYZ'
    'cfg_fr3OriginRPY'
    'cfg_fr3Axis'
    'cfg_fr3Joint8OriginXYZ'
    'cfg_fr3Joint8OriginRPY'
    'cfg_T_link8_CL'
    'cfg_T_CL_CR'
    'cfg_numericalEpsilon'
};

constantPositions = {
    [25 105 135 135]
    [25 155 135 185]
    [25 205 135 235]
    [25 255 135 285]
    [25 305 135 335]
    [25 355 135 385]
    [25 405 135 435]
    [25 455 135 485]
    [25 505 135 535]
};

for constantIndex = 1:numel(constantNames)
    add_block( ...
        'simulink/Sources/Constant', ...
        [subsystemPath '/' constantNames{constantIndex}], ...
        'Value', constantValues{constantIndex}, ...
        'OutDataTypeStr', 'double', ...
        'SampleTime', '-1', ...
        'Position', constantPositions{constantIndex});
end

%% MATLAB Function
functionBlockPath = [subsystemPath '/FR3 Camera Kinematics'];

add_block( ...
    'simulink/User-Defined Functions/MATLAB Function', ...
    functionBlockPath, ...
    'Position', [205 45 565 575]);

chart = find( ...
    sfroot, ...
    '-isa', 'Stateflow.EMChart', ...
    'Path', functionBlockPath);

if isempty(chart)
    error('StereoIBVS:MATLABFunctionNotFound', ...
        '无法找到 MATLAB Function：%s', functionBlockPath);
end

chart.Script = fr3CameraKinematicsCode();

%% 输出端口
outputDefinitions = {
    'pWCL',                         1,  '[3 1]'
    'RWCL',                         2,  '[3 3]'
    'pWCR',                         3,  '[3 1]'
    'RWCR',                         4,  '[3 3]'
    'JL',                           5,  '[6 7]'
    'JR',                           6,  '[6 7]'
    'rankJL',                       7,  '1'
    'rcondJL',                      8,  '1'
    'rotationOrthogonalityError',   9,  '1'
    'baselineLength',              10,  '1'
    'baselineError',               11,  '1'
    'stereoRotationError',         12,  '1'
    'pWLink8',                     13,  '[3 1]'
    'RWLink8',                     14,  '[3 3]'
    'kinematicsValid',             15,  '1'
};

outputY = 45:38:(45 + 38*(size(outputDefinitions,1)-1));

for outputIndex = 1:size(outputDefinitions,1)
    addOutport( ...
        subsystemPath, ...
        outputDefinitions{outputIndex,1}, ...
        outputDefinitions{outputIndex,2}, ...
        outputDefinitions{outputIndex,3}, ...
        [665 outputY(outputIndex) 695 outputY(outputIndex)+14]);
end

%% 内部输入连线
add_line( ...
    subsystemPath, ...
    'q/1', ...
    'FR3 Camera Kinematics/1', ...
    'autorouting', 'on');

for constantIndex = 1:numel(constantNames)
    add_line( ...
        subsystemPath, ...
        [constantNames{constantIndex} '/1'], ...
        sprintf('FR3 Camera Kinematics/%d', constantIndex + 1), ...
        'autorouting', 'on');
end

%% 内部输出连线
for outputIndex = 1:size(outputDefinitions,1)
    add_line( ...
        subsystemPath, ...
        sprintf('FR3 Camera Kinematics/%d', outputIndex), ...
        [outputDefinitions{outputIndex,1} '/1'], ...
        'autorouting', 'on');
end

%% 保存
save_system(modelName);

fprintf('\n04_FR3_Camera_Kinematics 重建完成。\n');
fprintf('模型：%s\n', modelFile);
fprintf('外部输入：q（7×1）。\n');
fprintf('配置输入：9个 Constant 块。\n');
fprintf('输出：15个，包含 V2 运动学诊断和 kinematicsValid。\n');
fprintf('注意：本脚本没有连接 Core 顶层线路。\n');
end


function addInport(parentPath, blockName, portNumber, dimensions, position)
add_block( ...
    'simulink/Sources/In1', ...
    [parentPath '/' blockName], ...
    'Port', num2str(portNumber), ...
    'PortDimensions', dimensions, ...
    'OutDataTypeStr', 'double', ...
    'Position', position);
end


function addOutport(parentPath, blockName, portNumber, dimensions, position)
add_block( ...
    'simulink/Sinks/Out1', ...
    [parentPath '/' blockName], ...
    'Port', num2str(portNumber), ...
    'PortDimensions', dimensions, ...
    'Position', position);
end


function deleteIfExists(blockPath)
if getSimulinkBlockHandle(blockPath) ~= -1
    delete_block(blockPath);
end
end


function code = fr3CameraKinematicsCode()
code = sprintf([ ...
'function [ ...\n' ...
'    pWCL, ...\n' ...
'    RWCL, ...\n' ...
'    pWCR, ...\n' ...
'    RWCR, ...\n' ...
'    JL, ...\n' ...
'    JR, ...\n' ...
'    rankJL, ...\n' ...
'    rcondJL, ...\n' ...
'    rotationOrthogonalityError, ...\n' ...
'    baselineLength, ...\n' ...
'    baselineError, ...\n' ...
'    stereoRotationError, ...\n' ...
'    pWLink8, ...\n' ...
'    RWLink8, ...\n' ...
'    kinematicsValid] = fcn( ...\n' ...
'    q, ...\n' ...
'    cfg_T_W_B, ...\n' ...
'    cfg_fr3OriginXYZ, ...\n' ...
'    cfg_fr3OriginRPY, ...\n' ...
'    cfg_fr3Axis, ...\n' ...
'    cfg_fr3Joint8OriginXYZ, ...\n' ...
'    cfg_fr3Joint8OriginRPY, ...\n' ...
'    cfg_T_link8_CL, ...\n' ...
'    cfg_T_CL_CR, ...\n' ...
'    cfg_numericalEpsilon)\n' ...
'%%#codegen\n' ...
'%% V2 FR3双目相机运动学与诊断。\n' ...
'\n' ...
'%% 固定尺寸输出\n' ...
'pWCL = zeros(3,1);\n' ...
'RWCL = eye(3);\n' ...
'pWCR = zeros(3,1);\n' ...
'RWCR = eye(3);\n' ...
'JL = zeros(6,7);\n' ...
'JR = zeros(6,7);\n' ...
'rankJL = 0;\n' ...
'rcondJL = 0;\n' ...
'rotationOrthogonalityError = 0;\n' ...
'baselineLength = 0;\n' ...
'baselineError = 0;\n' ...
'stereoRotationError = 0;\n' ...
'pWLink8 = zeros(3,1);\n' ...
'RWLink8 = eye(3);\n' ...
'kinematicsValid = false;\n' ...
'\n' ...
'%% 输入整理和检查\n' ...
'qSafe = reshape(q,7,1);\n' ...
'\n' ...
'inputValid = ...\n' ...
'    all(isfinite(qSafe)) && ...\n' ...
'    all(isfinite(cfg_T_W_B(:))) && ...\n' ...
'    all(isfinite(cfg_fr3OriginXYZ(:))) && ...\n' ...
'    all(isfinite(cfg_fr3OriginRPY(:))) && ...\n' ...
'    all(isfinite(cfg_fr3Axis(:))) && ...\n' ...
'    all(isfinite(cfg_fr3Joint8OriginXYZ(:))) && ...\n' ...
'    all(isfinite(cfg_fr3Joint8OriginRPY(:))) && ...\n' ...
'    all(isfinite(cfg_T_link8_CL(:))) && ...\n' ...
'    all(isfinite(cfg_T_CL_CR(:))) && ...\n' ...
'    isfinite(cfg_numericalEpsilon) && ...\n' ...
'    cfg_numericalEpsilon > 0;\n' ...
'\n' ...
'if ~inputValid\n' ...
'    return;\n' ...
'end\n' ...
'\n' ...
'%% FR3正运动学\n' ...
'T = reshape(cfg_T_W_B,4,4);\n' ...
'jointOriginsW = zeros(3,7);\n' ...
'jointAxesW = zeros(3,7);\n' ...
'\n' ...
'for jointIndex = 1:7\n' ...
'    T = T * makeTransform( ...\n' ...
'        cfg_fr3OriginXYZ(:,jointIndex), ...\n' ...
'        cfg_fr3OriginRPY(:,jointIndex));\n' ...
'\n' ...
'    jointOriginsW(:,jointIndex) = T(1:3,4);\n' ...
'\n' ...
'    axisLocal = cfg_fr3Axis(:,jointIndex);\n' ...
'    axisNorm = norm(axisLocal);\n' ...
'\n' ...
'    if ~isfinite(axisNorm) || ...\n' ...
'            axisNorm <= cfg_numericalEpsilon\n' ...
'        return;\n' ...
'    end\n' ...
'\n' ...
'    jointAxesW(:,jointIndex) = ...\n' ...
'        T(1:3,1:3) * axisLocal;\n' ...
'\n' ...
'    T = T * axisRotation(axisLocal,qSafe(jointIndex));\n' ...
'end\n' ...
'\n' ...
'%% link8固定变换\n' ...
'T_W_link8 = T * makeTransform( ...\n' ...
'    cfg_fr3Joint8OriginXYZ, ...\n' ...
'    cfg_fr3Joint8OriginRPY);\n' ...
'\n' ...
'pWLink8 = T_W_link8(1:3,4);\n' ...
'RWLink8 = T_W_link8(1:3,1:3);\n' ...
'\n' ...
'%% 左右相机位姿\n' ...
'T_W_CL = T_W_link8 * reshape(cfg_T_link8_CL,4,4);\n' ...
'T_W_CR = T_W_CL * reshape(cfg_T_CL_CR,4,4);\n' ...
'\n' ...
'pWCL = T_W_CL(1:3,4);\n' ...
'RWCL = T_W_CL(1:3,1:3);\n' ...
'pWCR = T_W_CR(1:3,4);\n' ...
'RWCR = T_W_CR(1:3,1:3);\n' ...
'\n' ...
'%% 左相机光心的世界坐标Jacobian\n' ...
'JWLeft = zeros(6,7);\n' ...
'\n' ...
'for jointIndex = 1:7\n' ...
'    JWLeft(1:3,jointIndex) = cross( ...\n' ...
'        jointAxesW(:,jointIndex), ...\n' ...
'        pWCL-jointOriginsW(:,jointIndex));\n' ...
'\n' ...
'    JWLeft(4:6,jointIndex) = ...\n' ...
'        jointAxesW(:,jointIndex);\n' ...
'end\n' ...
'\n' ...
'%% 转换到左相机坐标系\n' ...
'R_CL_W = RWCL'';\n' ...
'\n' ...
'JL = [ ...\n' ...
'    R_CL_W, zeros(3,3)\n' ...
'    zeros(3,3), R_CL_W\n' ...
'    ] * JWLeft;\n' ...
'\n' ...
'%% 从左相机Jacobian转换到右相机坐标系\n' ...
'R_CL_CR = cfg_T_CL_CR(1:3,1:3);\n' ...
'p_CL_CR = cfg_T_CL_CR(1:3,4);\n' ...
'\n' ...
'JR = [ ...\n' ...
'    R_CL_CR'', -R_CL_CR''*skew3(p_CL_CR)\n' ...
'    zeros(3,3), R_CL_CR''\n' ...
'    ] * JL;\n' ...
'\n' ...
'%% 有限值检查\n' ...
'finiteResult = ...\n' ...
'    all(isfinite(pWCL)) && ...\n' ...
'    all(isfinite(RWCL(:))) && ...\n' ...
'    all(isfinite(pWCR)) && ...\n' ...
'    all(isfinite(RWCR(:))) && ...\n' ...
'    all(isfinite(JL(:))) && ...\n' ...
'    all(isfinite(JR(:))) && ...\n' ...
'    all(isfinite(pWLink8)) && ...\n' ...
'    all(isfinite(RWLink8(:)));\n' ...
'\n' ...
'if ~finiteResult\n' ...
'    pWCL = zeros(3,1);\n' ...
'    RWCL = eye(3);\n' ...
'    pWCR = zeros(3,1);\n' ...
'    RWCR = eye(3);\n' ...
'    JL = zeros(6,7);\n' ...
'    JR = zeros(6,7);\n' ...
'    pWLink8 = zeros(3,1);\n' ...
'    RWLink8 = eye(3);\n' ...
'    return;\n' ...
'end\n' ...
'\n' ...
'%% V2运动学诊断\n' ...
'rankJL = rank(JL);\n' ...
'rcondJL = rcond(JL*JL'');\n' ...
'\n' ...
'rotationOrthogonalityError = max([ ...\n' ...
'    norm(RWCL''*RWCL-eye(3),''fro''), ...\n' ...
'    norm(RWCR''*RWCR-eye(3),''fro''), ...\n' ...
'    norm(RWLink8''*RWLink8-eye(3),''fro'')]);\n' ...
'\n' ...
'baselineLength = norm(pWCR-pWCL);\n' ...
'baselineError = abs( ...\n' ...
'    baselineLength-norm(p_CL_CR));\n' ...
'\n' ...
'stereoRotationError = norm( ...\n' ...
'    RWCR-RWCL*R_CL_CR, ...\n' ...
'    ''fro'');\n' ...
'\n' ...
'%% 最终有效性判断\n' ...
'rotationTolerance = max( ...\n' ...
'    1e-6, ...\n' ...
'    100*cfg_numericalEpsilon);\n' ...
'\n' ...
'diagnosticsFinite = ...\n' ...
'    isfinite(rankJL) && ...\n' ...
'    isfinite(rcondJL) && ...\n' ...
'    isfinite(rotationOrthogonalityError) && ...\n' ...
'    isfinite(baselineLength) && ...\n' ...
'    isfinite(baselineError) && ...\n' ...
'    isfinite(stereoRotationError);\n' ...
'\n' ...
'rotationValid = ...\n' ...
'    rotationOrthogonalityError <= rotationTolerance && ...\n' ...
'    abs(det(RWCL)-1) <= rotationTolerance && ...\n' ...
'    abs(det(RWCR)-1) <= rotationTolerance && ...\n' ...
'    abs(det(RWLink8)-1) <= rotationTolerance;\n' ...
'\n' ...
'kinematicsValid = ...\n' ...
'    diagnosticsFinite && ...\n' ...
'    rotationValid;\n' ...
'\n' ...
'if ~kinematicsValid\n' ...
'    return;\n' ...
'end\n' ...
'end\n' ...
'\n' ...
'\n' ...
'function T = makeTransform(xyz,rpy)\n' ...
'T = eye(4);\n' ...
'T(1:3,1:3) = ...\n' ...
'    rotz3(rpy(3)) * ...\n' ...
'    roty3(rpy(2)) * ...\n' ...
'    rotx3(rpy(1));\n' ...
'T(1:3,4) = xyz;\n' ...
'end\n' ...
'\n' ...
'\n' ...
'function T = axisRotation(axis,q)\n' ...
'axisNorm = norm(axis);\n' ...
'unitAxis = axis / axisNorm;\n' ...
'K = skew3(unitAxis);\n' ...
'R = eye(3) + ...\n' ...
'    sin(q)*K + ...\n' ...
'    (1-cos(q))*(K*K);\n' ...
'T = eye(4);\n' ...
'T(1:3,1:3) = R;\n' ...
'end\n' ...
'\n' ...
'\n' ...
'function R = rotx3(angle)\n' ...
'c = cos(angle);\n' ...
's = sin(angle);\n' ...
'R = [ ...\n' ...
'    1, 0, 0\n' ...
'    0, c, -s\n' ...
'    0, s, c\n' ...
'    ];\n' ...
'end\n' ...
'\n' ...
'\n' ...
'function R = roty3(angle)\n' ...
'c = cos(angle);\n' ...
's = sin(angle);\n' ...
'R = [ ...\n' ...
'    c, 0, s\n' ...
'    0, 1, 0\n' ...
'    -s, 0, c\n' ...
'    ];\n' ...
'end\n' ...
'\n' ...
'\n' ...
'function R = rotz3(angle)\n' ...
'c = cos(angle);\n' ...
's = sin(angle);\n' ...
'R = [ ...\n' ...
'    c, -s, 0\n' ...
'    s, c, 0\n' ...
'    0, 0, 1\n' ...
'    ];\n' ...
'end\n' ...
'\n' ...
'\n' ...
'function S = skew3(vector)\n' ...
'S = [ ...\n' ...
'    0, -vector(3), vector(2)\n' ...
'    vector(3), 0, -vector(1)\n' ...
'    -vector(2), vector(1), 0\n' ...
'    ];\n' ...
'end\n']);
end
