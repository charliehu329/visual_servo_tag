function build_01_input_validity()
%BUILD_01_INPUT_VALIDITY 重建 stereo_ibvs_core/01_Input_Validity
%
% 目标目录：
%   simulink/build/core/build_01_input_validity.m
%
% Core 内部约定：
%   - 焦距输入单位：mm
%   - visionMeasurementNewRaw：视觉消息 IsNew
%   - focalLengthIsNewRaw：焦距反馈消息 IsNew
%
% 本脚本只重建 01_Input_Validity，不连接 Core 顶层线路。

%% 路径
buildDir = fileparts(mfilename('fullpath'));
simulinkDir = fileparts(fileparts(buildDir));
modelFile = fullfile(simulinkDir, 'core', 'stereo_ibvs_core.slx');

if ~isfile(modelFile)
    error('StereoIBVS:CoreModelNotFound', ...
        '找不到 Core 模型：%s', modelFile);
end

[~, modelName] = fileparts(modelFile);
load_system(modelFile);

subsystemPath = [modelName '/01_Input_Validity'];

%% 删除旧子系统并重新创建
if getSimulinkBlockHandle(subsystemPath) ~= -1
    delete_block(subsystemPath);
end

add_block( ...
    'simulink/Ports & Subsystems/Subsystem', ...
    subsystemPath, ...
    'Position', [260 120 540 530], ...
    'BackgroundColor', 'lightBlue');

% 删除 Subsystem 默认端口。
deleteIfExists([subsystemPath '/In1']);
deleteIfExists([subsystemPath '/Out1']);

%% 输入端口
addInport(subsystemPath, ...
    'qRaw', 1, '[7 1]', [35 45 65 59]);

addInport(subsystemPath, ...
    'visionFeatureRaw', 2, '[8 1]', [35 95 65 109]);

addInport(subsystemPath, ...
    'focalLengthMeasuredMmRaw', 3, '[2 1]', [35 145 65 159]);

addInport(subsystemPath, ...
    'visionMeasurementNewRaw', 4, '1', [35 195 65 209]);

addInport(subsystemPath, ...
    'focalLengthIsNewRaw', 5, '1', [35 245 65 259]);

addInport(subsystemPath, ...
    'controllerEnableRaw', 6, '1', [35 295 65 309]);

addInport(subsystemPath, ...
    'resetRaw', 7, '1', [35 345 65 359]);

%% 配置常量
add_block( ...
    'simulink/Sources/Constant', ...
    [subsystemPath '/Initial Joint Position'], ...
    'Value', 'cfg_q0', ...
    'OutDataTypeStr', 'double', ...
    'Position', [35 395 105 425]);

add_block( ...
    'simulink/Sources/Constant', ...
    [subsystemPath '/Initial Focal Length Mm'], ...
    'Value', 'cfg_focalLength0Mm', ...
    'OutDataTypeStr', 'double', ...
    'Position', [35 450 105 480]);

%% MATLAB Function
functionBlockPath = [subsystemPath '/Input Validity'];

add_block( ...
    'simulink/User-Defined Functions/MATLAB Function', ...
    functionBlockPath, ...
    'Position', [155 35 465 495]);

chart = find( ...
    sfroot, ...
    '-isa', 'Stateflow.EMChart', ...
    'Path', functionBlockPath);

if isempty(chart)
    error('StereoIBVS:MATLABFunctionNotFound', ...
        '无法找到 MATLAB Function：%s', functionBlockPath);
end

chart.Script = inputValidityCode();

%% 输出端口
addOutport(subsystemPath, ...
    'q', 1, '[7 1]', [555 55 585 69]);

addOutport(subsystemPath, ...
    'visionFeature', 2, '[8 1]', [555 110 585 124]);

addOutport(subsystemPath, ...
    'focalLengthMeasuredMm', 3, '[2 1]', [555 165 585 179]);

addOutport(subsystemPath, ...
    'visionMeasurementNewSafe', 4, '1', [555 220 585 234]);

addOutport(subsystemPath, ...
    'focalLengthIsNewSafe', 5, '1', [555 275 585 289]);

addOutport(subsystemPath, ...
    'controllerEnableSafe', 6, '1', [555 330 585 344]);

addOutport(subsystemPath, ...
    'resetSafe', 7, '1', [555 385 585 399]);

addOutport(subsystemPath, ...
    'inputDataValid', 8, '1', [555 440 585 454]);

%% 内部连线
inputNames = {
    'qRaw'
    'visionFeatureRaw'
    'focalLengthMeasuredMmRaw'
    'visionMeasurementNewRaw'
    'focalLengthIsNewRaw'
    'controllerEnableRaw'
    'resetRaw'
    'Initial Joint Position'
    'Initial Focal Length Mm'
};

for inputIndex = 1:numel(inputNames)
    add_line( ...
        subsystemPath, ...
        [inputNames{inputIndex} '/1'], ...
        sprintf('Input Validity/%d', inputIndex), ...
        'autorouting', 'on');
end

outputNames = {
    'q'
    'visionFeature'
    'focalLengthMeasuredMm'
    'visionMeasurementNewSafe'
    'focalLengthIsNewSafe'
    'controllerEnableSafe'
    'resetSafe'
    'inputDataValid'
};

for outputIndex = 1:numel(outputNames)
    add_line( ...
        subsystemPath, ...
        sprintf('Input Validity/%d', outputIndex), ...
        [outputNames{outputIndex} '/1'], ...
        'autorouting', 'on');
end

%% 保存
save_system(modelName);

fprintf('\n01_Input_Validity 重建完成。\n');
fprintf('模型：%s\n', modelFile);
fprintf('输入：7个，输出：8个。\n');
fprintf('注意：本脚本没有连接 Core 顶层线路。\n');
end


function addInport(parentPath, blockName, portNumber, dimensions, position)
add_block( ...
    'simulink/Sources/In1', ...
    [parentPath '/' blockName], ...
    'Port', num2str(portNumber), ...
    'PortDimensions', dimensions, ...
    'OutDataTypeStr', 'double', ...
    'Position', position);
end


function addOutport(parentPath, blockName, portNumber, dimensions, position)
add_block( ...
    'simulink/Sinks/Out1', ...
    [parentPath '/' blockName], ...
    'Port', num2str(portNumber), ...
    'PortDimensions', dimensions, ...
    'Position', position);
end


function deleteIfExists(blockPath)
if getSimulinkBlockHandle(blockPath) ~= -1
    delete_block(blockPath);
end
end


function code = inputValidityCode()
code = sprintf([ ...
'function [ ...\n' ...
'    q, ...\n' ...
'    visionFeature, ...\n' ...
'    focalLengthMeasuredMm, ...\n' ...
'    visionMeasurementNewSafe, ...\n' ...
'    focalLengthIsNewSafe, ...\n' ...
'    controllerEnableSafe, ...\n' ...
'    resetSafe, ...\n' ...
'    inputDataValid] = fcn( ...\n' ...
'    qRaw, ...\n' ...
'    visionFeatureRaw, ...\n' ...
'    focalLengthMeasuredMmRaw, ...\n' ...
'    visionMeasurementNewRaw, ...\n' ...
'    focalLengthIsNewRaw, ...\n' ...
'    controllerEnableRaw, ...\n' ...
'    resetRaw, ...\n' ...
'    qInitial, ...\n' ...
'    focalLength0Mm)\n' ...
'%%#codegen\n' ...
'%% 检查并保存最近一次合法的关节、视觉和焦距输入。\n' ...
'%% Core内部焦距统一使用mm。\n' ...
'\n' ...
'persistent lastQ\n' ...
'persistent lastVisionFeature\n' ...
'persistent lastFocalLengthMeasuredMm\n' ...
'\n' ...
'initialQ = reshape(qInitial,7,1);\n' ...
'initialFocalLengthMm = reshape(focalLength0Mm,2,1);\n' ...
'\n' ...
'if isempty(lastQ)\n' ...
'    lastQ = initialQ;\n' ...
'    lastVisionFeature = zeros(8,1);\n' ...
'    lastFocalLengthMeasuredMm = initialFocalLengthMm;\n' ...
'end\n' ...
'\n' ...
'qCandidate = reshape(qRaw,7,1);\n' ...
'visionCandidate = reshape(visionFeatureRaw,8,1);\n' ...
'focalCandidate = reshape(focalLengthMeasuredMmRaw,2,1);\n' ...
'\n' ...
'qValid = all(isfinite(qCandidate));\n' ...
'visionValid = all(isfinite(visionCandidate));\n' ...
'focalValid = all(isfinite(focalCandidate));\n' ...
'visionNewValid = isfinite(visionMeasurementNewRaw);\n' ...
'focalNewValid = isfinite(focalLengthIsNewRaw);\n' ...
'enableValid = isfinite(controllerEnableRaw);\n' ...
'resetValid = isfinite(resetRaw);\n' ...
'\n' ...
'visionMeasurementNewSafe = double( ...\n' ...
'    visionNewValid && visionMeasurementNewRaw > 0.5);\n' ...
'\n' ...
'focalLengthIsNewSafe = double( ...\n' ...
'    focalNewValid && focalLengthIsNewRaw > 0.5);\n' ...
'\n' ...
'if qValid\n' ...
'    lastQ = qCandidate;\n' ...
'end\n' ...
'\n' ...
'if visionValid && visionMeasurementNewSafe > 0.5\n' ...
'    lastVisionFeature = visionCandidate;\n' ...
'end\n' ...
'\n' ...
'if focalValid && focalLengthIsNewSafe > 0.5\n' ...
'    lastFocalLengthMeasuredMm = focalCandidate;\n' ...
'end\n' ...
'\n' ...
'q = lastQ;\n' ...
'visionFeature = lastVisionFeature;\n' ...
'focalLengthMeasuredMm = lastFocalLengthMeasuredMm;\n' ...
'\n' ...
'inputDataValid = double( ...\n' ...
'    qValid && ...\n' ...
'    visionValid && ...\n' ...
'    focalValid && ...\n' ...
'    visionNewValid && ...\n' ...
'    focalNewValid && ...\n' ...
'    enableValid && ...\n' ...
'    resetValid);\n' ...
'\n' ...
'controllerEnableSafe = double( ...\n' ...
'    inputDataValid > 0.5 && ...\n' ...
'    controllerEnableRaw > 0.5);\n' ...
'\n' ...
'if resetValid\n' ...
'    resetSafe = double(resetRaw > 0.5);\n' ...
'else\n' ...
'    resetSafe = 0;\n' ...
'end\n' ...
'\n' ...
'if resetSafe > 0.5\n' ...
'    lastQ = initialQ;\n' ...
'    lastVisionFeature = zeros(8,1);\n' ...
'    lastFocalLengthMeasuredMm = initialFocalLengthMm;\n' ...
'\n' ...
'    q = lastQ;\n' ...
'    visionFeature = lastVisionFeature;\n' ...
'    focalLengthMeasuredMm = lastFocalLengthMeasuredMm;\n' ...
'    visionMeasurementNewSafe = 0;\n' ...
'    focalLengthIsNewSafe = 0;\n' ...
'    controllerEnableSafe = 0;\n' ...
'end\n' ...
'end\n']);
end
