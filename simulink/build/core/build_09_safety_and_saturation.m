function build_09_safety_and_saturation(modelName)
%BUILD_09_SAFETY_AND_SATURATION Rebuild 09 safety subsystem.
%
% Usage:
%   build_09_safety_and_saturation
%   build_09_safety_and_saturation('stereo_ibvs_core')
%
% The builder:
%   1. Keeps the Core top-level subsystem block.
%   2. Clears and rebuilds only the subsystem contents.
%   3. Builds signal Inports, cfg Constant blocks, MATLAB Function and Outports.
%   4. Does not connect or modify Core top-level lines.

if nargin < 1 || strlength(string(modelName)) == 0
    modelName = 'stereo_ibvs_core';
end

modelName = char(erase(string(modelName), '.slx'));

if ~bdIsLoaded(modelName)
    load_system(modelName);
end

%% Locate the existing 09 subsystem without creating a duplicate.
preferredName = '09_Safety_and_Saturation';
legacyName = '09_Safety_Supervisor';

preferredPath = [modelName '/' preferredName];
legacyPath = [modelName '/' legacyName];

if getSimulinkBlockHandle(preferredPath) ~= -1
    subsystemName = preferredName;
    subsystemPath = preferredPath;
elseif getSimulinkBlockHandle(legacyPath) ~= -1
    subsystemName = legacyName;
    subsystemPath = legacyPath;
else
    subsystemName = preferredName;
    subsystemPath = preferredPath;

    add_block( ...
        'simulink/Ports & Subsystems/Subsystem', ...
        subsystemPath, ...
        'Position', [1640 250 1940 700]);
end

% Keep the top-level subsystem block and replace only its internal content.
Simulink.SubSystem.deleteContents(subsystemPath);
set_param(subsystemPath, 'TreatAsAtomicUnit', 'on');

%% External signal inputs
inputNames = { ...
    'qDotCmd', ...
    'fDotMmLimited', ...
    'q', ...
    'focalLengthMeasuredMm', ...
    'validLeft', ...
    'focalLengthFresh', ...
    'cameraModelValid', ...
    'kinematicsValid', ...
    'JL', ...
    'controllerEnableSafe'};

inputDimensions = { ...
    '[7 1]', ...
    '[2 1]', ...
    '[7 1]', ...
    '[2 1]', ...
    '1', ...
    '1', ...
    '1', ...
    '1', ...
    '[6 7]', ...
    '1'};

for inputIndex = 1:numel(inputNames)
    y = 50 + (inputIndex - 1) * 72;
    blockPath = [subsystemPath '/' inputNames{inputIndex}];

    add_block( ...
        'simulink/Sources/In1', ...
        blockPath, ...
        'Port', num2str(inputIndex), ...
        'OutDataTypeStr', 'double', ...
        'PortDimensions', inputDimensions{inputIndex}, ...
        'Position', [30 y 220 y+22]);
end

%% Configuration Constants
constantNames = { ...
    'qDotMax', ...
    'qDotAlgorithmMax', ...
    'qMin', ...
    'qMax', ...
    'qLimitSoftMargin', ...
    'cartesianLinearSpeedMax', ...
    'focalRateGuaranteedMmPerSec', ...
    'focalRateAbsoluteMaxMmPerSec', ...
    'focalLengthWorkingMinMm', ...
    'focalLengthWorkingMaxMm', ...
    'focalLengthHardwareMinMm', ...
    'focalLengthHardwareMaxMm', ...
    'Ts', ...
    'numericalEpsilon', ...
    'armControlCalibrationReady', ...
    'zoomControlCalibrationReady', ...
    'armControlEnable', ...
    'zoomControlEnable'};

constantValues = { ...
    'cfg.qDotMax', ...
    'cfg.qDotAlgorithmMax', ...
    'cfg.qMin', ...
    'cfg.qMax', ...
    'cfg.qLimitSoftMargin', ...
    'cfg.cartesianLinearSpeedMax', ...
    'cfg.focalRateGuaranteedMmPerSec', ...
    'cfg.focalRateAbsoluteMaxMmPerSec', ...
    'cfg.focalLengthWorkingMinMm', ...
    'cfg.focalLengthWorkingMaxMm', ...
    'cfg.focalLengthHardwareMinMm', ...
    'cfg.focalLengthHardwareMaxMm', ...
    'cfg.Ts', ...
    'cfg.numericalEpsilon', ...
    'cfg.armControlCalibrationReady', ...
    'cfg.zoomControlCalibrationReady', ...
    'cfg.armControlEnable', ...
    'cfg.zoomControlEnable'};

for constantIndex = 1:numel(constantNames)
    y = 790 + (constantIndex - 1) * 54;
    blockPath = [subsystemPath '/' constantNames{constantIndex}];

    add_block( ...
        'simulink/Sources/Constant', ...
        blockPath, ...
        'Value', constantValues{constantIndex}, ...
        'OutDataTypeStr', 'double', ...
        'SampleTime', '-1', ...
        'Position', [255 y 510 y+28]);
end

%% MATLAB Function
functionBlockName = 'Apply Safety and Saturation';
functionBlockPath = [subsystemPath '/' functionBlockName];

add_block( ...
    'simulink/User-Defined Functions/MATLAB Function', ...
    functionBlockPath, ...
    'Position', [610 35 1055 1740]);

stateflowRoot = sfroot;
chart = find( ...
    stateflowRoot, ...
    '-isa', 'Stateflow.EMChart', ...
    'Path', functionBlockPath);

if isempty(chart)
    error( ...
        'StereoIBVS:SafetyChartNotFound', ...
        '无法找到 MATLAB Function Chart：%s', ...
        functionBlockPath);
end

chart.Script = safetyAndSaturationCode();
drawnow;

%% Outputs
outputNames = { ...
    'qDotApplied', ...
    'fDotMmApplied', ...
    'qSaturationFlag', ...
    'focalRateSaturationFlag', ...
    'jointVelocitySaturationFlag', ...
    'jointVelocitySaturationAny', ...
    'jointLimitWarning', ...
    'cartesianSpeed', ...
    'cartesianSpeedScale', ...
    'cartesianSpeedSaturationFlag', ...
    'focalLengthCommandMm', ...
    'focalRateUsageGuaranteed', ...
    'focalRateUsageHard', ...
    'focalRateHardViolation', ...
    'safetyValid'};

outputDimensions = { ...
    '[7 1]', ...
    '[2 1]', ...
    '1', ...
    '1', ...
    '[7 1]', ...
    '1', ...
    '[7 1]', ...
    '1', ...
    '1', ...
    '1', ...
    '[2 1]', ...
    '[2 1]', ...
    '[2 1]', ...
    '[2 1]', ...
    '1'};

for outputIndex = 1:numel(outputNames)
    y = 50 + (outputIndex - 1) * 106;
    blockPath = [subsystemPath '/' outputNames{outputIndex}];

    add_block( ...
        'simulink/Sinks/Out1', ...
        blockPath, ...
        'Port', num2str(outputIndex), ...
        'PortDimensions', outputDimensions{outputIndex}, ...
        'Position', [1245 y 1485 y+22]);
end

%% Internal connections
% Signal inputs -> MATLAB Function inputs 1...10.
for inputIndex = 1:numel(inputNames)
    addNamedLine( ...
        subsystemPath, ...
        [inputNames{inputIndex} '/1'], ...
        [functionBlockName '/' num2str(inputIndex)], ...
        inputNames{inputIndex});
end

% Constant blocks -> MATLAB Function inputs 11...28.
for constantIndex = 1:numel(constantNames)
    functionInputIndex = numel(inputNames) + constantIndex;
    signalName = ['cfg_' constantNames{constantIndex}];

    addNamedLine( ...
        subsystemPath, ...
        [constantNames{constantIndex} '/1'], ...
        [functionBlockName '/' num2str(functionInputIndex)], ...
        signalName);
end

% MATLAB Function outputs -> subsystem outputs.
for outputIndex = 1:numel(outputNames)
    addNamedLine( ...
        subsystemPath, ...
        [functionBlockName '/' num2str(outputIndex)], ...
        [outputNames{outputIndex} '/1'], ...
        outputNames{outputIndex});
end

%% Interface verification
functionPorts = get_param(functionBlockPath, 'PortHandles');

if numel(functionPorts.Inport) ~= 28
    error( ...
        'StereoIBVS:SafetyInputCount', ...
        'MATLAB Function 应有28个输入，实际为%d个。', ...
        numel(functionPorts.Inport));
end

if numel(functionPorts.Outport) ~= 15
    error( ...
        'StereoIBVS:SafetyOutputCount', ...
        'MATLAB Function 应有15个输出，实际为%d个。', ...
        numel(functionPorts.Outport));
end

save_system(modelName);

fprintf('\n%s 构建完成。\n', subsystemName);
fprintf('输入：10个信号 + 18个 Constant。\n');
fprintf('输出：15个。\n');
fprintf('未修改 Core 顶层连线。\n');
end


function addNamedLine(systemPath, sourcePort, destinationPort, signalName)
lineHandle = add_line( ...
    systemPath, ...
    sourcePort, ...
    destinationPort, ...
    'autorouting', 'on');

if ~isempty(signalName)
    try
        set_param(lineHandle, 'Name', signalName);
    catch
        % Line naming is cosmetic; a routing branch must not stop the build.
    end
end
end


function code = safetyAndSaturationCode()
codeLines = { ...
        'function [ ...'; ...
        '    qDotApplied, ...'; ...
        '    fDotMmApplied, ...'; ...
        '    qSaturationFlag, ...'; ...
        '    focalRateSaturationFlag, ...'; ...
        '    jointVelocitySaturationFlag, ...'; ...
        '    jointVelocitySaturationAny, ...'; ...
        '    jointLimitWarning, ...'; ...
        '    cartesianSpeed, ...'; ...
        '    cartesianSpeedScale, ...'; ...
        '    cartesianSpeedSaturationFlag, ...'; ...
        '    focalLengthCommandMm, ...'; ...
        '    focalRateUsageGuaranteed, ...'; ...
        '    focalRateUsageHard, ...'; ...
        '    focalRateHardViolation, ...'; ...
        '    safetyValid] = fcn( ...'; ...
        '    qDotCmd, ...'; ...
        '    fDotMmLimited, ...'; ...
        '    q, ...'; ...
        '    focalLengthMeasuredMm, ...'; ...
        '    validLeft, ...'; ...
        '    focalLengthFresh, ...'; ...
        '    cameraModelValid, ...'; ...
        '    kinematicsValid, ...'; ...
        '    JL, ...'; ...
        '    controllerEnableSafe, ...'; ...
        '    cfg_qDotMax, ...'; ...
        '    cfg_qDotAlgorithmMax, ...'; ...
        '    cfg_qMin, ...'; ...
        '    cfg_qMax, ...'; ...
        '    cfg_qLimitSoftMargin, ...'; ...
        '    cfg_cartesianLinearSpeedMax, ...'; ...
        '    cfg_focalRateGuaranteedMmPerSec, ...'; ...
        '    cfg_focalRateAbsoluteMaxMmPerSec, ...'; ...
        '    cfg_focalLengthWorkingMinMm, ...'; ...
        '    cfg_focalLengthWorkingMaxMm, ...'; ...
        '    cfg_focalLengthHardwareMinMm, ...'; ...
        '    cfg_focalLengthHardwareMaxMm, ...'; ...
        '    cfg_Ts, ...'; ...
        '    cfg_numericalEpsilon, ...'; ...
        '    cfg_armControlCalibrationReady, ...'; ...
        '    cfg_zoomControlCalibrationReady, ...'; ...
        '    cfg_armControlEnable, ...'; ...
        '    cfg_zoomControlEnable)'; ...
        '%#codegen'; ...
        '% V2 Safety and Saturation'; ...
        '% JL顺序：[linear velocity; angular velocity]'; ...
        ''; ...
        '%% 固定尺寸输出'; ...
        'qDotApplied = zeros(7,1);'; ...
        'fDotMmApplied = zeros(2,1);'; ...
        ''; ...
        'qSaturationFlag = 0;'; ...
        'focalRateSaturationFlag = 0;'; ...
        ''; ...
        'jointVelocitySaturationFlag = zeros(7,1);'; ...
        'jointVelocitySaturationAny = 0;'; ...
        'jointLimitWarning = zeros(7,1);'; ...
        ''; ...
        'cartesianSpeed = 0;'; ...
        'cartesianSpeedScale = 1;'; ...
        'cartesianSpeedSaturationFlag = 0;'; ...
        ''; ...
        'focalLengthCommandMm = zeros(2,1);'; ...
        'focalRateUsageGuaranteed = zeros(2,1);'; ...
        'focalRateUsageHard = zeros(2,1);'; ...
        'focalRateHardViolation = zeros(2,1);'; ...
        ''; ...
        'safetyValid = 0;'; ...
        ''; ...
        '%% 固定尺寸输入'; ...
        'qDotRequested = reshape(qDotCmd,7,1);'; ...
        'fDotRequested = reshape(fDotMmLimited,2,1);'; ...
        'qMeasured = reshape(q,7,1);'; ...
        'focalMeasured = reshape(focalLengthMeasuredMm,2,1);'; ...
        ''; ...
        '%% 参数检查'; ...
        'tol = max(cfg_numericalEpsilon,1e-12);'; ...
        ''; ...
        'inputFinite = ...'; ...
        '    all(isfinite(qDotRequested)) && ...'; ...
        '    all(isfinite(fDotRequested)) && ...'; ...
        '    all(isfinite(qMeasured)) && ...'; ...
        '    all(isfinite(focalMeasured)) && ...'; ...
        '    all(isfinite(JL(:))) && ...'; ...
        '    isfinite(validLeft) && ...'; ...
        '    isfinite(focalLengthFresh) && ...'; ...
        '    isfinite(cameraModelValid) && ...'; ...
        '    isfinite(kinematicsValid) && ...'; ...
        '    isfinite(controllerEnableSafe);'; ...
        ''; ...
        'parameterFinite = ...'; ...
        '    all(isfinite(cfg_qDotMax)) && ...'; ...
        '    all(isfinite(cfg_qDotAlgorithmMax)) && ...'; ...
        '    all(isfinite(cfg_qMin)) && ...'; ...
        '    all(isfinite(cfg_qMax)) && ...'; ...
        '    isfinite(cfg_qLimitSoftMargin) && ...'; ...
        '    isfinite(cfg_cartesianLinearSpeedMax) && ...'; ...
        '    isfinite(cfg_focalRateGuaranteedMmPerSec) && ...'; ...
        '    isfinite(cfg_focalRateAbsoluteMaxMmPerSec) && ...'; ...
        '    all(isfinite(cfg_focalLengthWorkingMinMm)) && ...'; ...
        '    all(isfinite(cfg_focalLengthWorkingMaxMm)) && ...'; ...
        '    all(isfinite(cfg_focalLengthHardwareMinMm)) && ...'; ...
        '    all(isfinite(cfg_focalLengthHardwareMaxMm)) && ...'; ...
        '    isfinite(cfg_Ts) && ...'; ...
        '    isfinite(cfg_numericalEpsilon) && ...'; ...
        '    isfinite(cfg_armControlCalibrationReady) && ...'; ...
        '    isfinite(cfg_zoomControlCalibrationReady) && ...'; ...
        '    isfinite(cfg_armControlEnable) && ...'; ...
        '    isfinite(cfg_zoomControlEnable);'; ...
        ''; ...
        'parameterRangesValid = ...'; ...
        '    all(cfg_qDotMax >= 0) && ...'; ...
        '    all(cfg_qDotAlgorithmMax >= 0) && ...'; ...
        '    all(cfg_qMax > cfg_qMin) && ...'; ...
        '    cfg_qLimitSoftMargin >= 0 && ...'; ...
        '    all(2*cfg_qLimitSoftMargin < ...'; ...
        '        (cfg_qMax-cfg_qMin)) && ...'; ...
        '    cfg_cartesianLinearSpeedMax > 0 && ...'; ...
        '    cfg_focalRateGuaranteedMmPerSec > 0 && ...'; ...
        '    cfg_focalRateAbsoluteMaxMmPerSec >= ...'; ...
        '        cfg_focalRateGuaranteedMmPerSec && ...'; ...
        '    all(cfg_focalLengthHardwareMaxMm > ...'; ...
        '        cfg_focalLengthHardwareMinMm) && ...'; ...
        '    all(cfg_focalLengthWorkingMinMm >= ...'; ...
        '        cfg_focalLengthHardwareMinMm) && ...'; ...
        '    all(cfg_focalLengthWorkingMaxMm <= ...'; ...
        '        cfg_focalLengthHardwareMaxMm) && ...'; ...
        '    all(cfg_focalLengthWorkingMaxMm > ...'; ...
        '        cfg_focalLengthWorkingMinMm) && ...'; ...
        '    cfg_Ts > 0;'; ...
        ''; ...
        'if ~(inputFinite && parameterFinite && parameterRangesValid)'; ...
        '    qSaturationFlag = 1;'; ...
        '    focalRateSaturationFlag = 1;'; ...
        '    jointVelocitySaturationFlag = ones(7,1);'; ...
        '    jointVelocitySaturationAny = 1;'; ...
        '    cartesianSpeedScale = 0;'; ...
        '    return;'; ...
        'end'; ...
        ''; ...
        'safetyValid = 1;'; ...
        ''; ...
        '%% 当前焦距'; ...
        'if cameraModelValid > 0.5'; ...
        '    focalLengthCommandMm = focalMeasured;'; ...
        'end'; ...
        ''; ...
        '%% 关节限位诊断'; ...
        'for i = 1:7'; ...
        '    nearLowerLimit = ...'; ...
        '        qMeasured(i) <= ...'; ...
        '        cfg_qMin(i)+cfg_qLimitSoftMargin;'; ...
        ''; ...
        '    nearUpperLimit = ...'; ...
        '        qMeasured(i) >= ...'; ...
        '        cfg_qMax(i)-cfg_qLimitSoftMargin;'; ...
        ''; ...
        '    outsideLowerLimit = ...'; ...
        '        qMeasured(i) <= cfg_qMin(i);'; ...
        ''; ...
        '    outsideUpperLimit = ...'; ...
        '        qMeasured(i) >= cfg_qMax(i);'; ...
        ''; ...
        '    jointLimitWarning(i) = double( ...'; ...
        '        nearLowerLimit || ...'; ...
        '        nearUpperLimit || ...'; ...
        '        outsideLowerLimit || ...'; ...
        '        outsideUpperLimit);'; ...
        'end'; ...
        ''; ...
        '%% 控制许可'; ...
        'armPermission = ...'; ...
        '    controllerEnableSafe > 0.5 && ...'; ...
        '    cfg_armControlEnable > 0.5 && ...'; ...
        '    cfg_armControlCalibrationReady > 0.5 && ...'; ...
        '    validLeft > 0.5 && ...'; ...
        '    cameraModelValid > 0.5 && ...'; ...
        '    kinematicsValid > 0.5;'; ...
        ''; ...
        'zoomPermission = ...'; ...
        '    controllerEnableSafe > 0.5 && ...'; ...
        '    cfg_zoomControlEnable > 0.5 && ...'; ...
        '    cfg_zoomControlCalibrationReady > 0.5 && ...'; ...
        '    validLeft > 0.5 && ...'; ...
        '    cameraModelValid > 0.5 && ...'; ...
        '    focalLengthFresh > 0.5;'; ...
        ''; ...
        '%% 关节速度限制'; ...
        'if armPermission'; ...
        ''; ...
        '    qDotLimit = min( ...'; ...
        '        abs(cfg_qDotMax), ...'; ...
        '        abs(cfg_qDotAlgorithmMax));'; ...
        ''; ...
        '    qCandidate = min( ...'; ...
        '        max(qDotRequested,-qDotLimit), ...'; ...
        '        qDotLimit);'; ...
        ''; ...
        '    for i = 1:7'; ...
        ''; ...
        '        nearLowerLimit = ...'; ...
        '            qMeasured(i) <= ...'; ...
        '            cfg_qMin(i)+cfg_qLimitSoftMargin;'; ...
        ''; ...
        '        nearUpperLimit = ...'; ...
        '            qMeasured(i) >= ...'; ...
        '            cfg_qMax(i)-cfg_qLimitSoftMargin;'; ...
        ''; ...
        '        if nearLowerLimit && qCandidate(i) < 0'; ...
        '            qCandidate(i) = 0;'; ...
        '        end'; ...
        ''; ...
        '        if nearUpperLimit && qCandidate(i) > 0'; ...
        '            qCandidate(i) = 0;'; ...
        '        end'; ...
        '    end'; ...
        ''; ...
        '    %% 笛卡尔线速度限制'; ...
        '    cameraTwistCandidate = JL*qCandidate;'; ...
        '    cartesianSpeedCandidate = ...'; ...
        '        norm(cameraTwistCandidate(1:3));'; ...
        ''; ...
        '    if ~isfinite(cartesianSpeedCandidate)'; ...
        ''; ...
        '        qCandidate = zeros(7,1);'; ...
        '        cartesianSpeedScale = 0;'; ...
        '        cartesianSpeedSaturationFlag = 1;'; ...
        '        safetyValid = 0;'; ...
        ''; ...
        '    elseif cartesianSpeedCandidate > ...'; ...
        '            cfg_cartesianLinearSpeedMax'; ...
        ''; ...
        '        cartesianSpeedScale = ...'; ...
        '            cfg_cartesianLinearSpeedMax / ...'; ...
        '            max(cartesianSpeedCandidate,tol);'; ...
        ''; ...
        '        qCandidate = ...'; ...
        '            cartesianSpeedScale*qCandidate;'; ...
        ''; ...
        '        cartesianSpeedSaturationFlag = 1;'; ...
        '    end'; ...
        ''; ...
        '    qDotApplied = min( ...'; ...
        '        max(qCandidate,-qDotLimit), ...'; ...
        '        qDotLimit);'; ...
        ''; ...
        '    finalCameraTwist = JL*qDotApplied;'; ...
        '    cartesianSpeed = norm(finalCameraTwist(1:3));'; ...
        'end'; ...
        ''; ...
        '%% Zoom硬限速违规诊断'; ...
        'focalRateHardViolation = double( ...'; ...
        '    abs(fDotRequested) > ...'; ...
        '    cfg_focalRateAbsoluteMaxMmPerSec);'; ...
        ''; ...
        '%% Zoom焦距速度限制'; ...
        'if zoomPermission'; ...
        ''; ...
        '    fCandidate = min( ...'; ...
        '        max(fDotRequested, ...'; ...
        '            -cfg_focalRateAbsoluteMaxMmPerSec), ...'; ...
        '        cfg_focalRateAbsoluteMaxMmPerSec);'; ...
        ''; ...
        '    for i = 1:2'; ...
        ''; ...
        '        focalCurrent = focalMeasured(i);'; ...
        ''; ...
        '        workingMin = ...'; ...
        '            cfg_focalLengthWorkingMinMm(i);'; ...
        '        workingMax = ...'; ...
        '            cfg_focalLengthWorkingMaxMm(i);'; ...
        ''; ...
        '        hardwareMin = ...'; ...
        '            cfg_focalLengthHardwareMinMm(i);'; ...
        '        hardwareMax = ...'; ...
        '            cfg_focalLengthHardwareMaxMm(i);'; ...
        ''; ...
        '        %% 禁止继续远离工作范围'; ...
        '        if focalCurrent <= workingMin && ...'; ...
        '                fCandidate(i) < 0'; ...
        '            fCandidate(i) = 0;'; ...
        '        end'; ...
        ''; ...
        '        if focalCurrent >= workingMax && ...'; ...
        '                fCandidate(i) > 0'; ...
        '            fCandidate(i) = 0;'; ...
        '        end'; ...
        ''; ...
        '        %% 下一周期工作范围预测'; ...
        '        nextFocalLength = ...'; ...
        '            focalCurrent + ...'; ...
        '            cfg_Ts*fCandidate(i);'; ...
        ''; ...
        '        if focalCurrent >= workingMin && ...'; ...
        '                nextFocalLength < workingMin'; ...
        ''; ...
        '            fCandidate(i) = ...'; ...
        '                (workingMin-focalCurrent)/cfg_Ts;'; ...
        ''; ...
        '        elseif focalCurrent <= workingMax && ...'; ...
        '                nextFocalLength > workingMax'; ...
        ''; ...
        '            fCandidate(i) = ...'; ...
        '                (workingMax-focalCurrent)/cfg_Ts;'; ...
        '        end'; ...
        ''; ...
        '        %% 硬件范围最终保护'; ...
        '        nextFocalLength = ...'; ...
        '            focalCurrent + ...'; ...
        '            cfg_Ts*fCandidate(i);'; ...
        ''; ...
        '        if nextFocalLength < hardwareMin'; ...
        ''; ...
        '            fCandidate(i) = ...'; ...
        '                (hardwareMin-focalCurrent)/cfg_Ts;'; ...
        ''; ...
        '        elseif nextFocalLength > hardwareMax'; ...
        ''; ...
        '            fCandidate(i) = ...'; ...
        '                (hardwareMax-focalCurrent)/cfg_Ts;'; ...
        '        end'; ...
        '    end'; ...
        ''; ...
        '    fDotMmApplied = min( ...'; ...
        '        max(fCandidate, ...'; ...
        '            -cfg_focalRateAbsoluteMaxMmPerSec), ...'; ...
        '        cfg_focalRateAbsoluteMaxMmPerSec);'; ...
        'end'; ...
        ''; ...
        '%% 最终有限值保护'; ...
        'if any(~isfinite(qDotApplied)) || ...'; ...
        '        ~isfinite(cartesianSpeed) || ...'; ...
        '        ~isfinite(cartesianSpeedScale)'; ...
        ''; ...
        '    qDotApplied = zeros(7,1);'; ...
        '    cartesianSpeed = 0;'; ...
        '    cartesianSpeedScale = 0;'; ...
        '    cartesianSpeedSaturationFlag = 1;'; ...
        '    safetyValid = 0;'; ...
        'end'; ...
        ''; ...
        'if any(~isfinite(fDotMmApplied))'; ...
        '    fDotMmApplied = zeros(2,1);'; ...
        '    safetyValid = 0;'; ...
        'end'; ...
        ''; ...
        '%% 饱和诊断'; ...
        'jointVelocitySaturationFlag = double( ...'; ...
        '    abs(qDotApplied-qDotRequested) > tol);'; ...
        ''; ...
        'jointVelocitySaturationAny = double( ...'; ...
        '    any(jointVelocitySaturationFlag > 0.5));'; ...
        ''; ...
        'qSaturationFlag = ...'; ...
        '    jointVelocitySaturationAny;'; ...
        ''; ...
        'focalRateSaturationFlag = double( ...'; ...
        '    any(abs(fDotMmApplied-fDotRequested) > tol));'; ...
        ''; ...
        '%% Zoom使用率'; ...
        'focalRateUsageGuaranteed = ...'; ...
        '    abs(fDotMmApplied) / ...'; ...
        '    cfg_focalRateGuaranteedMmPerSec;'; ...
        ''; ...
        'focalRateUsageHard = ...'; ...
        '    abs(fDotMmApplied) / ...'; ...
        '    cfg_focalRateAbsoluteMaxMmPerSec;'; ...
        ''; ...
        '%% 下一周期焦距命令'; ...
        'if cameraModelValid > 0.5'; ...
        ''; ...
        '    focalLengthCommandMm = min( ...'; ...
        '        max( ...'; ...
        '            focalMeasured + ...'; ...
        '            cfg_Ts*fDotMmApplied, ...'; ...
        '            cfg_focalLengthHardwareMinMm), ...'; ...
        '        cfg_focalLengthHardwareMaxMm);'; ...
        ''; ...
        'else'; ...
        '    focalLengthCommandMm = zeros(2,1);'; ...
        'end'; ...
        ''; ...
