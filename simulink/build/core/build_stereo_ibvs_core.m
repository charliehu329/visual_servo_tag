function buildFunctionWrapper(parentModel,blockName,position,inputs,constants,functionName,functionCode,outputs)
subsystemPath = [parentModel '/' blockName];
createEmptySubsystem(subsystemPath,position);
numberOfInputs = size(inputs,1);
numberOfConstants = size(constants,1);
numberOfOutputs = size(outputs,1);
maximumRows = max([numberOfInputs+numberOfConstants,numberOfOutputs,4]);
functionTop = 35;
functionBottom = max(220,35+42*maximumRows);
for inputIndex = 1:numberOfInputs
    y = 45 + 42*(inputIndex-1);
    addInport(subsystemPath,inputs{inputIndex,1},inputIndex,inputs{inputIndex,2},[25 y 55 y+14]);
end
for constantIndex = 1:numberOfConstants
    y = 45 + 42*(numberOfInputs+constantIndex-1);
    add_block('simulink/Sources/Constant', ...
        [subsystemPath '/' constants{constantIndex,1}], ...
        'Value',constants{constantIndex,2}, ...
        'OutDataTypeStr','double', ...
        'SampleTime','-1', ...
        'Position',[90 y-7 285 y+21]);
end
functionBlockPath = [subsystemPath '/' functionName];
addMatlabFunctionBlock(subsystemPath,functionName,[350 functionTop 760 functionBottom],functionCode);
for outputIndex = 1:numberOfOutputs
    y = 45 + 42*(outputIndex-1);
    addOutport(subsystemPath,outputs{outputIndex,1},outputIndex,outputs{outputIndex,2},[850 y 880 y+14]);
end
for inputIndex = 1:numberOfInputs
    safeAddLine(subsystemPath,[inputs{inputIndex,1} '/1'],sprintf('%s/%d',functionName,inputIndex));
end
for constantIndex = 1:numberOfConstants
    safeAddLine(subsystemPath,[constants{constantIndex,1} '/1'], ...
        sprintf('%s/%d',functionName,numberOfInputs+constantIndex));
end
for outputIndex = 1:numberOfOutputs
    safeAddLine(subsystemPath,sprintf('%s/%d',functionName,outputIndex),[outputs{outputIndex,1} '/1']);
end
end
function createEmptySubsystem(subsystemPath,position)
if getSimulinkBlockHandle(subsystemPath) ~= -1
    delete_block(subsystemPath);
end
add_block('simulink/Ports & Subsystems/Subsystem',subsystemPath, ...
    'Position',position,'BackgroundColor','white');
deleteIfExists([subsystemPath '/In1']);
deleteIfExists([subsystemPath '/Out1']);
end
function addInterfacePorts(parentPath,inputs,outputs)
for inputIndex = 1:size(inputs,1)
    y = 45 + 42*(inputIndex-1);
    addInport(parentPath,inputs{inputIndex,1},inputIndex,inputs{inputIndex,2},[25 y 55 y+14]);
end
for outputIndex = 1:size(outputs,1)
    y = 45 + 34*(outputIndex-1);
    addOutport(parentPath,outputs{outputIndex,1},outputIndex,outputs{outputIndex,2},[1480 y 1510 y+14]);
end
end
function addMatlabFunctionBlock(parentPath,blockName,position,code)
blockPath = [parentPath '/' blockName];
add_block('simulink/User-Defined Functions/MATLAB Function',blockPath,'Position',position);
chart = find(sfroot,'-isa','Stateflow.EMChart','Path',blockPath);
if isempty(chart)
    error('StereoIBVS:MATLABFunctionNotFound','无法找到MATLAB Function：%s',blockPath);
end
chart.Script = code;
end
