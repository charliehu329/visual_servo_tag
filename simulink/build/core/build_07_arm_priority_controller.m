function build_07_arm_priority_controller(modelName)
%BUILD_07_ARM_PRIORITY_CONTROLLER
% 重建 stereo_ibvs_core/07_Arm_Priority_Controller。
%
% 作用：
% 1. 创建13个外部信号输入；
% 2. 用Constant块提供cLd、rhoD和全部cfg参数；
% 3. cameraModelValid、kinematicsValid、controllerEnable经AND后生成armEnable；
% 4. 写入V2 Final Arm Priority Controller MATLAB Function；
% 5. 创建9个输出并完成子系统内部连线；
% 6. 不修改Core顶层连线。
%
% 使用：
%   stereo_ibvs_config
%   open_system('stereo_ibvs_core')
%   build_07_arm_priority_controller('stereo_ibvs_core')

if nargin < 1 || strlength(string(modelName)) == 0
    modelName = 'stereo_ibvs_core';
end

modelName = char(modelName);
load_system(modelName);

subsystemName = '07_Arm_Priority_Controller';
subsystemPath = [modelName '/' subsystemName];

if getSimulinkBlockHandle(subsystemPath) == -1
    add_block( ...
        'simulink/Ports & Subsystems/Subsystem', ...
        subsystemPath, ...
        'Position', [700 260 920 680]);
end

Simulink.SubSystem.deleteContents(subsystemPath);

%% 输入端口
inputNames = {
    'q'
    'cLMeasured'
    'rhoHatL'
    'vHatLeft'
    'JL'
    'validLeft'
    'ekfPredictionValid'
    'cameraModelValid'
    'kinematicsValid'
    'controllerEnable'
    'centerEnable'
    'depthEnable'
    'depthTaskWeight'
};

inputY = [ ...
     40  90 140 190 240 ...
    310 360 430 480 530 ...
    600 650 700];

for index = 1:numel(inputNames)
    addInport( ...
        subsystemPath, ...
        inputNames{index}, ...
        index, ...
        [30 inputY(index) 150 inputY(index)+20]);
end

%% cLd、rhoD
addConstant( ...
    subsystemPath, ...
    'cLd', ...
    'cfg.centerDesired', ...
    [430 40 580 70]);

addConstant( ...
    subsystemPath, ...
    'rhoD', ...
    'cfg.rhoD', ...
    [430 90 580 120]);

%% armEnable许可链
andPath = [subsystemPath '/Arm Permission AND'];
add_block( ...
    'simulink/Logic and Bit Operations/Logical Operator', ...
    andPath, ...
    'Operator', 'AND', ...
    'Inputs', '3', ...
    'Position', [320 445 375 515]);

doublePath = [subsystemPath '/armEnable double'];
add_block( ...
    'simulink/Signal Attributes/Data Type Conversion', ...
    doublePath, ...
    'OutDataTypeStr', 'double', ...
    'Position', [410 460 510 500]);

%% cfg Constant
constantNames = {
    'cfg_numericalEpsilon'
    'cfg_robustEnable'
    'cfg_betaC'
    'cfg_epsilonC'
    'cfg_Kc'
    'cfg_lambdaC'
    'cfg_betaRho'
    'cfg_epsilonRho'
    'cfg_kRho'
    'cfg_lambdaRho'
    'cfg_nullspaceEnable'
    'cfg_kNull'
    'cfg_qMid'
    'cfg_leftOnlyCenterControlEnable'
};

constantValues = {
    'cfg.numericalEpsilon'
    'cfg.robustEnable'
    'cfg.betaC'
    'cfg.epsilonC'
    'cfg.Kc'
    'cfg.lambdaC'
    'cfg.betaRho'
    'cfg.epsilonRho'
    'cfg.kRho'
    'cfg.lambdaRho'
    'cfg.nullspaceEnable'
    'cfg.kNull'
    'cfg.qMid'
    'double(cfg.leftOnlyCenterControlEnable)'
};

constantX = [620 790];
constantStartY = 250;
constantStepY = 55;

for index = 1:numel(constantNames)
    column = 1 + (index > 7);
    row = index;
    if column == 2
        row = index - 7;
    end

    x1 = constantX(column);
    y1 = constantStartY + (row-1)*constantStepY;

    addConstant( ...
        subsystemPath, ...
        constantNames{index}, ...
        constantValues{index}, ...
        [x1 y1 x1+145 y1+30]);
end

%% MATLAB Function
functionBlockName = 'Final Arm Priority Controller';
functionPath = [subsystemPath '/' functionBlockName];

add_block( ...
    'simulink/User-Defined Functions/MATLAB Function', ...
    functionPath, ...
    'Position', [1030 45 1300 825]);

controllerCode = strjoin({
    'function [qDotCmd,centerError,depthError,rcondJc,rcondArho,qDotCenter,qDotDepthRaw,qDotDepthWeighted,qDotNullUsed] = ...' ...
    '    fcn(q,cLMeasured,rhoHatL,vHatLeft,JL,cLd,rhoD,validLeft,ekfPredictionValid,centerEnable,depthEnable,armEnable,depthTaskWeight, ...' ...
    '    cfg_numericalEpsilon,cfg_robustEnable,cfg_betaC,cfg_epsilonC,cfg_Kc,cfg_lambdaC,cfg_betaRho,cfg_epsilonRho,cfg_kRho,cfg_lambdaRho,cfg_nullspaceEnable,cfg_kNull,cfg_qMid,cfg_leftOnlyCenterControlEnable)' ...
    '%#codegen' ...
    '% 代码作用：' ...
    '% 左相机归一化中心为主任务，逆深度为次任务。' ...
    '% 支持目标速度前馈、鲁棒项、阻尼伪逆和关节中位零空间任务。' ...
    '' ...
    'qDotCmd = zeros(7,1);' ...
    'centerError = zeros(2,1);' ...
    'depthError = 0;' ...
    'rcondJc = 0;' ...
    'rcondArho = 0;' ...
    'qDotCenter = zeros(7,1);' ...
    'qDotDepthRaw = zeros(7,1);' ...
    'qDotDepthWeighted = zeros(7,1);' ...
    'qDotNullUsed = zeros(7,1);' ...
    '' ...
    'x = cLMeasured(1);' ...
    'y = cLMeasured(2);' ...
    'rho = rhoD;' ...
    'vFeed = zeros(3,1);' ...
    '' ...
    'if ekfPredictionValid > 0.5 && ...' ...
    '        isfinite(rhoHatL) && rhoHatL > 0 && ...' ...
    '        all(isfinite(vHatLeft))' ...
    '    rho = max(rhoHatL,cfg_numericalEpsilon);' ...
    '    vFeed = vHatLeft;' ...
    'end' ...
    '' ...
    'centerError = [x;y] - cLd;' ...
    'depthError = rho - rhoD;' ...
    '' ...
    'Lc = [' ...
    '    -rho, 0, x*rho, x*y, -(1+x*x), y' ...
    '    0, -rho, y*rho, 1+y*y, -x*y, -x' ...
    '];' ...
    '' ...
    'Hc = rho*[1 0 -x;0 1 -y];' ...
    'bhatC = Hc*vFeed;' ...
    'Jc = Lc*JL;' ...
    '' ...
    'rc = zeros(2,1);' ...
    'if cfg_robustEnable > 0.5' ...
    '    rc = cfg_betaC*centerError / ...' ...
    '        (sqrt(centerError''*centerError)+cfg_epsilonC);' ...
    'end' ...
    '' ...
    'nuC = -cfg_Kc*centerError - bhatC - rc;' ...
    'Gc = Jc*Jc'' + cfg_lambdaC^2*eye(2);' ...
    'JcSharp = Jc''/Gc;' ...
    'qC = JcSharp*nuC;' ...
    'Nc = eye(7)-JcSharp*Jc;' ...
    'rcondJc = det(Gc) / ...' ...
    '    (trace(Gc)*trace(Gc)+cfg_numericalEpsilon);' ...
    'qDotCenter = qC;' ...
    '' ...
    'Lrho = [0 0 rho*rho rho*y -rho*x 0];' ...
    'Hrho = [0 0 -rho*rho];' ...
    'Jrho = Lrho*JL;' ...
    'bhatRho = Hrho*vFeed;' ...
    '' ...
    'rr = 0;' ...
    'if cfg_robustEnable > 0.5' ...
    '    rr = cfg_betaRho*depthError / ...' ...
    '        (abs(depthError)+cfg_epsilonRho);' ...
    'end' ...
    '' ...
    'nuRho = -cfg_kRho*depthError-bhatRho-rr;' ...
    'A = Jrho*Nc;' ...
    'denom = A*A''+cfg_lambdaRho^2;' ...
    'rcondArho = denom/(1+denom);' ...
    '' ...
    'w = min(max(depthTaskWeight,0),1) * ...' ...
    '    double(ekfPredictionValid > 0.5);' ...
    'qDotDepthRaw = Nc*A''*((nuRho-Jrho*qC)/denom);' ...
    'qDotDepthWeighted = w*qDotDepthRaw;' ...
    '' ...
    'qDot0 = zeros(7,1);' ...
    'if cfg_nullspaceEnable > 0.5' ...
    '    qDot0 = -cfg_kNull*(q-cfg_qMid);' ...
    'end' ...
    '' ...
    'ArhoDagger = A''/denom;' ...
    'NcRho = Nc*(eye(7)-ArhoDagger*A);' ...
    'Nnull = (1-w)*Nc+w*NcRho;' ...
    'qDotNullUsed = Nnull*qDot0;' ...
    '' ...
    'if centerEnable > 0.5' ...
    '    qDotCmd = qDotCenter+qDotNullUsed;' ...
    'end' ...
    '' ...
    'if centerEnable > 0.5 && depthEnable > 0.5' ...
    '    qDotCmd = qDotCenter+qDotDepthWeighted+qDotNullUsed;' ...
    'end' ...
    '' ...
    'if centerEnable <= 0.5 && depthEnable > 0.5' ...
    '    d = Jrho*Jrho''+cfg_lambdaRho^2;' ...
    '    qDotDepthRaw = Jrho''*(nuRho/d);' ...
    '    qDotDepthWeighted = w*qDotDepthRaw;' ...
    '    qDotCenter = zeros(7,1);' ...
    '    qDotNullUsed = zeros(7,1);' ...
    '    qDotCmd = qDotDepthWeighted;' ...
    'end' ...
    '' ...
    'centerAllowed = validLeft > 0.5 && ...' ...
    '    cfg_leftOnlyCenterControlEnable > 0.5;' ...
    '' ...
    'if ~centerAllowed || armEnable <= 0.5' ...
    '    qDotCmd = zeros(7,1);' ...
    '    qDotCenter = zeros(7,1);' ...
    '    qDotDepthWeighted = zeros(7,1);' ...
    '    qDotNullUsed = zeros(7,1);' ...
    'end' ...
    '' ...
    'if any(~isfinite(qDotCmd))' ...
    '    qDotCmd = zeros(7,1);' ...
    'end' ...
    'end'
}, newline);

setMATLABFunctionCode(functionPath, controllerCode);

%% 输出端口
outputNames = {
    'qDotCmd'
    'centerError'
    'depthError'
    'rcondJc'
    'rcondArho'
    'qDotCenter'
    'qDotDepthRaw'
    'qDotDepthWeighted'
    'qDotNullUsed'
};

outputY = [80 160 240 320 400 500 590 680 770];

for index = 1:numel(outputNames)
    addOutport( ...
        subsystemPath, ...
        outputNames{index}, ...
        index, ...
        [1420 outputY(index) 1540 outputY(index)+20]);
end

%% armEnable内部连线
add_line( ...
    subsystemPath, ...
    'cameraModelValid/1', ...
    'Arm Permission AND/1', ...
    'autorouting', 'on');

add_line( ...
    subsystemPath, ...
    'kinematicsValid/1', ...
    'Arm Permission AND/2', ...
    'autorouting', 'on');

add_line( ...
    subsystemPath, ...
    'controllerEnable/1', ...
    'Arm Permission AND/3', ...
    'autorouting', 'on');

add_line( ...
    subsystemPath, ...
    'Arm Permission AND/1', ...
    'armEnable double/1', ...
    'autorouting', 'on');

%% MATLAB Function输入连线
% 输入顺序必须与fcn签名完全一致。
functionSources = {
    'q/1'
    'cLMeasured/1'
    'rhoHatL/1'
    'vHatLeft/1'
    'JL/1'
    'cLd/1'
    'rhoD/1'
    'validLeft/1'
    'ekfPredictionValid/1'
    'centerEnable/1'
    'depthEnable/1'
    'armEnable double/1'
    'depthTaskWeight/1'
    'cfg_numericalEpsilon/1'
    'cfg_robustEnable/1'
    'cfg_betaC/1'
    'cfg_epsilonC/1'
    'cfg_Kc/1'
    'cfg_lambdaC/1'
    'cfg_betaRho/1'
    'cfg_epsilonRho/1'
    'cfg_kRho/1'
    'cfg_lambdaRho/1'
    'cfg_nullspaceEnable/1'
    'cfg_kNull/1'
    'cfg_qMid/1'
    'cfg_leftOnlyCenterControlEnable/1'
};

for index = 1:numel(functionSources)
    add_line( ...
        subsystemPath, ...
        functionSources{index}, ...
        [functionBlockName '/' num2str(index)], ...
        'autorouting', 'on');
end

%% MATLAB Function输出连线
for index = 1:numel(outputNames)
    add_line( ...
        subsystemPath, ...
        [functionBlockName '/' num2str(index)], ...
        [outputNames{index} '/1'], ...
        'autorouting', 'on');
end

set_param(subsystemPath, 'ZoomFactor', 'FitSystem');
save_system(modelName);

fprintf('\n07_Arm_Priority_Controller构建完成。\n');
fprintf('模型：%s\n', modelName);
fprintf('子系统：%s\n', subsystemPath);
fprintf('已创建：13个信号输入、16个Constant/许可块、1个MATLAB Function、9个输出。\n');
fprintf('未修改Core顶层连线。\n');
end


function addInport(parentPath, blockName, portNumber, position)
blockPath = [parentPath '/' blockName];

add_block( ...
    'simulink/Ports & Subsystems/In1', ...
    blockPath, ...
    'Port', num2str(portNumber), ...
    'Position', position);
end


function addOutport(parentPath, blockName, portNumber, position)
blockPath = [parentPath '/' blockName];

add_block( ...
    'simulink/Ports & Subsystems/Out1', ...
    blockPath, ...
    'Port', num2str(portNumber), ...
    'Position', position);
end


function addConstant(parentPath, blockName, valueExpression, position)
blockPath = [parentPath '/' blockName];

add_block( ...
    'simulink/Sources/Constant', ...
    blockPath, ...
    'Value', valueExpression, ...
    'OutDataTypeStr', 'double', ...
    'Position', position);
end


function setMATLABFunctionCode(blockPath, code)
rootObject = sfroot;
chartObject = rootObject.find( ...
    '-isa', 'Stateflow.EMChart', ...
    'Path', blockPath);

if isempty(chartObject)
    error( ...
        'StereoIBVS:MATLABFunctionChartNotFound', ...
        '找不到MATLAB Function对应的Stateflow.EMChart：%s', ...
        blockPath);
end

chartObject(1).Script = code;
end
