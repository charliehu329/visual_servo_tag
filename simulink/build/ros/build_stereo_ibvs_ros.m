function code = codeJointStateAdapter()
code = strjoin({ ...
    'function [qMeasured,qDotMeasured,jointStateFresh] = fcn( ...', ...
    '    jointNameBytesRaw, ...', ...
    '    jointNameLengthsRaw, ...', ...
    '    jointNameCountRaw, ...', ...
    '    positions, ...', ...
    '    positionCountRaw, ...', ...
    '    velocities, ...', ...
    '    velocityCountRaw, ...', ...
    '    messageIsNew, ...', ...
    '    resetSafe, ...', ...
    '    expectedNames, ...', ...
    '    timeoutFramesRaw)', ...
    '%#codegen', ...
    '% 本函数按JointState.name重排七个关节的位置和实测速度，并监控反馈超时。', ...
    '', ...
    'persistent lastQ', ...
    'persistent lastQDot', ...
    'persistent receivedOnce', ...
    'persistent framesSinceLastValidMessage', ...
    '', ...
    'timeoutFrames = max(1.0,floor(timeoutFramesRaw));', ...
    '', ...
    'if isempty(lastQ)', ...
    '    lastQ = zeros(7,1);', ...
    '    lastQDot = zeros(7,1);', ...
    '    receivedOnce = false;', ...
    '    framesSinceLastValidMessage = timeoutFrames;', ...
    'end', ...
    '', ...
    'if isfinite(resetSafe) && resetSafe > 0.5', ...
    '    lastQ = zeros(7,1);', ...
    '    lastQDot = zeros(7,1);', ...
    '    receivedOnce = false;', ...
    '    framesSinceLastValidMessage = timeoutFrames;', ...
    'end', ...
    '', ...
    'newValidMessage = false;', ...
    '', ...
    'if isfinite(messageIsNew) && messageIsNew > 0.5', ...
    '    jointNameBytes = reshape(jointNameBytesRaw,128,16);', ...
    '    jointNameLengths = reshape(jointNameLengthsRaw,16,1);', ...
    '    nameCount = min(double(jointNameCountRaw),16.0);', ...
    '    positionCount = min(double(positionCountRaw),double(numel(positions)));', ...
    '    velocityCount = min(double(velocityCountRaw),double(numel(velocities)));', ...
    '    sourceCount = floor(min(nameCount,min(positionCount,velocityCount)));', ...
    '    expectedNameLength = size(expectedNames,2);', ...
    '', ...
    '    candidateQ = zeros(7,1);', ...
    '    candidateQDot = zeros(7,1);', ...
    '    found = false(7,1);', ...
    '', ...
    '    for sourceIndex = 1:sourceCount', ...
    '        sourceNameLength = double(jointNameLengths(sourceIndex));', ...
    '', ...
    '        if sourceNameLength == expectedNameLength', ...
    '            sourceName = jointNameBytes(1:expectedNameLength,sourceIndex);', ...
    '', ...
    '            for targetIndex = 1:7', ...
    '                expectedName = expectedNames(targetIndex,:).'';', ...
    '', ...
    '                if all(sourceName == expectedName)', ...
    '                    if ~found(targetIndex)', ...
    '                        candidateQ(targetIndex) = positions(sourceIndex);', ...
    '                        candidateQDot(targetIndex) = velocities(sourceIndex);', ...
    '                        found(targetIndex) = true;', ...
    '                    end', ...
    '                end', ...
    '            end', ...
    '        end', ...
    '    end', ...
    '', ...
    '    newValidMessage = ...', ...
    '        all(found) && ...', ...
    '        all(isfinite(candidateQ)) && ...', ...
    '        all(isfinite(candidateQDot));', ...
    '', ...
    '    if newValidMessage', ...
    '        lastQ = candidateQ;', ...
    '        lastQDot = candidateQDot;', ...
    '        receivedOnce = true;', ...
    '        framesSinceLastValidMessage = 0;', ...
    '    end', ...
    'end', ...
    '', ...
    'if ~newValidMessage && receivedOnce', ...
    '    framesSinceLastValidMessage = min( ...', ...
    '        framesSinceLastValidMessage + 1, ...', ...
    '        timeoutFrames + 1);', ...
    'end', ...
    '', ...
    'qMeasured = lastQ;', ...
    'qDotMeasured = lastQDot;', ...
    'jointStateFresh = double( ...', ...
    '    receivedOnce && ...', ...
    '    framesSinceLastValidMessage < timeoutFrames);', ...
    'end' ...
    }, newline);
end


function code = codeStereoFeatureAdapter()
code = strjoin({ ...
    'function [ ...', ...
    '    visionFeature, ...', ...
    '    visionMeasurementNew, ...', ...
    '    leftMeasurementNew, ...', ...
    '    stereoMeasurementNew, ...', ...
    '    visionFresh] = fcn( ...', ...
    '    leftSequence, ...', ...
    '    rightSequence, ...', ...
    '    leftSec, ...', ...
    '    leftNanosec, ...', ...
    '    rightSec, ...', ...
    '    rightNanosec, ...', ...
    '    validLeftRaw, ...', ...
    '    validRightRaw, ...', ...
    '    uLeft, ...', ...
    '    vLeft, ...', ...
    '    uRight, ...', ...
    '    vRight, ...', ...
    '    scaleLeft, ...', ...
    '    scaleRight, ...', ...
    '    messageIsNew, ...', ...
    '    resetSafe, ...', ...
    '    timeoutFramesRaw, ...', ...
    '    maximumPairSkewSec)', ...
    '%#codegen', ...
    '% 本函数按左右帧序号识别真实新图像，并仅让新的合格双目配对触发EKF。', ...
    '% 左相机可以独立刷新中心控制；右相机更新不要求左相机同步到达。', ...
    '', ...
    'persistent lastFeature', ...
    'persistent lastLeftSequence', ...
    'persistent lastRightSequence', ...
    'persistent pairedLeftSequence', ...
    'persistent pairedRightSequence', ...
    'persistent lastLeftCaptureTime', ...
    'persistent lastRightCaptureTime', ...
    'persistent leftSeen', ...
    'persistent rightSeen', ...
    'persistent stereoPairSeen', ...
    'persistent framesSinceLastLeftFrame', ...
    '', ...
    'timeoutFrames = max(1.0,floor(timeoutFramesRaw));', ...
    '', ...
    'if isempty(lastFeature)', ...
    '    lastFeature = zeros(8,1);', ...
    '    lastLeftSequence = uint32(0);', ...
    '    lastRightSequence = uint32(0);', ...
    '    pairedLeftSequence = uint32(0);', ...
    '    pairedRightSequence = uint32(0);', ...
    '    lastLeftCaptureTime = 0;', ...
    '    lastRightCaptureTime = 0;', ...
    '    leftSeen = false;', ...
    '    rightSeen = false;', ...
    '    stereoPairSeen = false;', ...
    '    framesSinceLastLeftFrame = timeoutFrames;', ...
    'end', ...
    '', ...
    'visionMeasurementNew = 0;', ...
    'leftMeasurementNew = 0;', ...
    'stereoMeasurementNew = 0;', ...
    '', ...
    'resetRequested = ...', ...
    '    isfinite(resetSafe) && ...', ...
    '    resetSafe > 0.5;', ...
    '', ...
    'if resetRequested', ...
    '    lastFeature = zeros(8,1);', ...
    '    lastLeftSequence = uint32(0);', ...
    '    lastRightSequence = uint32(0);', ...
    '    pairedLeftSequence = uint32(0);', ...
    '    pairedRightSequence = uint32(0);', ...
    '    lastLeftCaptureTime = 0;', ...
    '    lastRightCaptureTime = 0;', ...
    '    leftSeen = false;', ...
    '    rightSeen = false;', ...
    '    stereoPairSeen = false;', ...
    '    framesSinceLastLeftFrame = timeoutFrames;', ...
    'else', ...
    '    leftChanged = false;', ...
    '    rightChanged = false;', ...
    '', ...
    '    if isfinite(messageIsNew) && messageIsNew > 0.5', ...
    '        leftChanged = ~leftSeen || leftSequence ~= lastLeftSequence;', ...
    '        rightChanged = ~rightSeen || rightSequence ~= lastRightSequence;', ...
    '', ...
    '        leftValuesFinite = ...', ...
    '            all(isfinite([double(leftSec);double(leftNanosec);uLeft;vLeft;scaleLeft]));', ...
    '', ...
    '        rightValuesFinite = ...', ...
    '            all(isfinite([double(rightSec);double(rightNanosec);uRight;vRight;scaleRight]));', ...
    '', ...
    '        if leftChanged && leftValuesFinite', ...
    '            lastLeftSequence = leftSequence;', ...
    '            lastLeftCaptureTime = ...', ...
    '                double(leftSec) + 1.0e-9*double(leftNanosec);', ...
    '            lastFeature(1) = double(logical(validLeftRaw));', ...
    '            lastFeature(3) = uLeft;', ...
    '            lastFeature(4) = vLeft;', ...
    '            lastFeature(7) = scaleLeft;', ...
    '            leftSeen = true;', ...
    '            framesSinceLastLeftFrame = 0;', ...
    '            leftMeasurementNew = 1;', ...
    '        else', ...
    '            leftChanged = false;', ...
    '        end', ...
    '', ...
    '        if rightChanged && rightValuesFinite', ...
    '            lastRightSequence = rightSequence;', ...
    '            lastRightCaptureTime = ...', ...
    '                double(rightSec) + 1.0e-9*double(rightNanosec);', ...
    '            lastFeature(2) = double(logical(validRightRaw));', ...
    '            lastFeature(5) = uRight;', ...
    '            lastFeature(6) = vRight;', ...
    '            lastFeature(8) = scaleRight;', ...
    '            rightSeen = true;', ...
    '        else', ...
    '            rightChanged = false;', ...
    '        end', ...
    '', ...
    '        visionMeasurementNew = double(leftChanged || rightChanged);', ...
    '', ...
    '        pairUnused = ...', ...
    '            leftSeen && ...', ...
    '            rightSeen && ...', ...
    '            (~stereoPairSeen || ...', ...
    '             (lastLeftSequence ~= pairedLeftSequence && ...', ...
    '              lastRightSequence ~= pairedRightSequence));', ...
    '', ...
    '        pairSkewValid = ...', ...
    '            isfinite(maximumPairSkewSec) && ...', ...
    '            maximumPairSkewSec >= 0 && ...', ...
    '            abs(lastLeftCaptureTime-lastRightCaptureTime) <= ...', ...
    '                maximumPairSkewSec;', ...
    '', ...
    '        if pairUnused && pairSkewValid', ...
    '            pairedLeftSequence = lastLeftSequence;', ...
    '            pairedRightSequence = lastRightSequence;', ...
    '            stereoPairSeen = true;', ...
    '            stereoMeasurementNew = 1;', ...
    '        end', ...
    '    end', ...
    '', ...
    '    if leftMeasurementNew < 0.5 && leftSeen', ...
    '        framesSinceLastLeftFrame = min( ...', ...
    '            framesSinceLastLeftFrame + 1, ...', ...
    '            timeoutFrames + 1);', ...
    '    end', ...
    'end', ...
    '', ...
    'visionFeature = lastFeature;', ...
    'visionFresh = double( ...', ...
    '    leftSeen && ...', ...
    '    framesSinceLastLeftFrame < timeoutFrames);', ...
    'end' ...
    }, newline);
end


function code = codeFocalLengthAdapter()
code = strjoin({ ...
    'function [focalLengthMeasuredMm,focalLengthIsNew] = fcn( ...', ...
    '    data, ...', ...
    '    dataInfo, ...', ...
    '    messageIsNew, ...', ...
    '    resetSafe)', ...
    '%#codegen', ...
    '% 本函数读取双目焦距消息的前两个元素，并仅转发合法的新消息。', ...
    '', ...
    'persistent lastFocalLengthMm', ...
    '', ...
    'if isempty(lastFocalLengthMm)', ...
    '    lastFocalLengthMm = zeros(2,1);', ...
    'end', ...
    '', ...
    'focalLengthIsNew = 0;', ...
    '', ...
    'if isfinite(resetSafe) && resetSafe > 0.5', ...
    '    lastFocalLengthMm = zeros(2,1);', ...
    'elseif isfinite(messageIsNew) && messageIsNew > 0.5', ...
    '    receivedLength = min(double(dataInfo.CurrentLength),double(numel(data)));', ...
    '', ...
    '    if receivedLength >= 2', ...
    '        candidate = reshape(data(1:2),2,1);', ...
    '', ...
    '        if all(isfinite(candidate))', ...
    '            lastFocalLengthMm = candidate;', ...
    '            focalLengthIsNew = 1;', ...
    '        end', ...
    '    end', ...
    'end', ...
    '', ...
    'focalLengthMeasuredMm = lastFocalLengthMm;', ...
    'end' ...
    }, newline);
end
