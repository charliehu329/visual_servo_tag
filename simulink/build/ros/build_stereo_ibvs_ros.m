function createEmptySubsystem(subsystemPath,position)
if getSimulinkBlockHandle(subsystemPath) ~= -1
    delete_block(subsystemPath);
end

add_block( ...
    'simulink/Ports & Subsystems/Subsystem', ...
    subsystemPath, ...
    'Position',position, ...
    'BackgroundColor','white');

deleteIfExists([subsystemPath '/In1']);
deleteIfExists([subsystemPath '/Out1']);
end


function addMatlabFunctionBlock(parentPath,blockName,position,code)
blockPath = [parentPath '/' blockName];

add_block( ...
    'simulink/User-Defined Functions/MATLAB Function', ...
    blockPath, ...
    'Position',position);

chart = find( ...
    sfroot, ...
    '-isa', ...
    'Stateflow.EMChart', ...
    'Path', ...
    blockPath);

if isempty(chart)
    error( ...
        'StereoIBVS:MATLABFunctionNotFound', ...
        '无法找到 MATLAB Function：%s', ...
        blockPath);
end

chart.Script = code;
end


