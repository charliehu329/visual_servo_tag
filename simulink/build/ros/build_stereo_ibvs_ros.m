function safeAddLine(parentPath,source,destination,varargin)
try
    lineHandle = add_line( ...
        parentPath, ...
        source, ...
        destination, ...
        'autorouting','on');

    if ~isempty(varargin)
        set_param( ...
            lineHandle, ...
            'Name', ...
            varargin{1});
    end
catch ME
    error( ...
        'StereoIBVS:LineConnectionFailed', ...
        '连接失败：%s -> %s\n%s', ...
        source, ...
        destination, ...
        ME.message);
end
end


function deleteIfExists(blockPath)
if getSimulinkBlockHandle(blockPath) ~= -1
    delete_block(blockPath);
end
end


function validateCustomMessageSupport(messageType,workspaceDir)
try
    ros2message(messageType);
catch ME
    error( ...
        'StereoIBVS:CustomMessageUnavailable', ...
        [ ...
        'MATLAB 尚未加载自定义消息 %s。\n' ...
        '请先接受 Xcode 许可证并配置 C++ 编译器，然后执行：\n' ...
        '  mex -setup C++\n' ...
        '  ros2genmsg(''%s'')\n' ...
        '原始错误：%s' ...
        ], ...
        messageType, ...
        workspaceDir, ...
        ME.message);
end
end


