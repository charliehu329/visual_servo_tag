%% 构建 Stereo IBVS ROS 2 包装模型
% 输入：
% 1. /franka/joint_states：按 JointState.name 重排关节角和实测关节速度；
% 2. 自定义 StereoFeatures：左右帧序号、采集时刻、有效位和图像特征；
% 3. 双目焦距反馈与外部复位消息。
%
% 输出：
% 1. 七维关节速度命令；
% 2. 两维焦距速度命令；
% 3. Core 诊断状态。
%
% 调用：
% run('simulink/build/ros/build_stereo_ibvs_ros.m')
%
% 方法：
% ROS 2 适配层只负责消息解包、事件判定、JointState 重排和消息封装；
% 控制与估计算法全部由 stereo_ibvs_core 模型引用执行。

clearvars;
clc;

scriptPath = mfilename('fullpath');
scriptDir = fileparts(scriptPath);
buildDir = fileparts(scriptDir);
simulinkDir = fileparts(buildDir);
repoDir = fileparts(simulinkDir);
workspaceDir = fileparts(repoDir);

configPath = fullfile( ...
    simulinkDir, ...
    'config', ...
    'stereo_ibvs_config.m');

coreDir = fullfile(simulinkDir,'core');
coreModelPath = fullfile( ...
    coreDir, ...
    'stereo_ibvs_core.slx');

outputDir = fullfile(simulinkDir,'ros2');
modelName = 'stereo_ibvs_ros';
outputModelPath = fullfile(outputDir,[modelName '.slx']);

generatedMessagePath = fullfile( ...
    workspaceDir, ...
    'matlab_msg_gen', ...
    computer('arch'), ...
    'install', ...
    'm');

if ~isfile(configPath)
    error( ...
        'StereoIBVS:ConfigNotFound', ...
        '找不到配置文件：%s', ...
        configPath);
end

if ~isfile(coreModelPath)
    error( ...
        'StereoIBVS:CoreModelNotFound', ...
        '找不到 Core 模型：%s', ...
        coreModelPath);
end

run(configPath);

addpath(coreDir);

if isfolder(generatedMessagePath)
    addpath(generatedMessagePath);
end

validateCustomMessageSupport( ...
    cfg.stereoFeatureMessageType, ...
    workspaceDir);

if ~isfolder(outputDir)
    mkdir(outputDir);
end

load_system('simulink');
load_system('ros2lib');
load_system(coreModelPath);

if bdIsLoaded(modelName)
    close_system(modelName,0);
end

if isfile(outputModelPath)
    backupDir = fullfile(outputDir,'backup');

    if ~isfolder(backupDir)
        mkdir(backupDir);
    end

    backupPath = fullfile( ...
        backupDir, ...
        sprintf( ...
            '%s_backup_%s.slx', ...
            modelName, ...
            char(datetime('now','Format','yyyyMMdd_HHmmss'))));

    copyfile(outputModelPath,backupPath);
end

new_system(modelName);
open_system(modelName);

set_param( ...
    modelName, ...
    'SolverType','Fixed-step', ...
    'Solver','FixedStepDiscrete', ...
    'FixedStep','cfg.Ts', ...
    'StartTime','0.0', ...
    'StopTime','inf', ...
    'SaveTime','off', ...
    'SaveOutput','off', ...
    'ReturnWorkspaceOutputs','off');

set_param( ...
    modelName, ...
    'InitFcn', ...
    modelInitCode());

buildInputAdapter( ...
    modelName, ...
    cfg);

add_block( ...
    'simulink/Ports & Subsystems/Model', ...
    [modelName '/Stereo IBVS Core'], ...
    'ModelName','stereo_ibvs_core', ...
    'Position',[980 110 1210 580]);

buildOutputAdapter( ...
    modelName, ...
    cfg);

for portIndex = 1:10
    safeAddLine( ...
        modelName, ...
        sprintf('ROS 2 Input Adapter/%d',portIndex), ...
        sprintf('Stereo IBVS Core/%d',portIndex));
end

for portIndex = 1:3
    safeAddLine( ...
        modelName, ...
        sprintf('Stereo IBVS Core/%d',portIndex), ...
        sprintf('ROS 2 Output Adapter/%d',portIndex));
end

% 先保存模型，使 InitFcn 能从模型绝对路径定位配置与自定义消息目录。
save_system(modelName,outputModelPath);

set_param( ...
    modelName, ...
    'SimulationCommand', ...
    'update');

save_system(modelName);
close_system(modelName,0);
close_system('stereo_ibvs_core',0);

fprintf('\nStereo IBVS ROS 2 包装模型构建完成：%s\n',outputModelPath);
fprintf('JointState：按 name 重排 q 和 qDotMeasured。\n');
fprintf('视觉事件：左帧独立更新，合格新配对触发 EKF。\n');
fprintf('Core接口：10个输入，3个输出。\n');


