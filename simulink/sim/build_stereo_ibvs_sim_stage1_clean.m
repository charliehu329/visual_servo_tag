function build_stereo_ibvs_sim_stage1_clean(projectDir)
% 代码作用：
% 只生成Stage 1离线仿真模型stereo_ibvs_sim_stage1.slx。
% 本脚本只读取stereo_ibvs_core.slx和stereo_ibvs_config.m，
% 不修改、不保存这两个文件，也不生成其他辅助文件。
%
% 输入参数含义：
% projectDir：
% simulink_new工程目录。可以省略，脚本会自动查找。
%
% 输出参数含义：
% 本函数没有输出参数。
% 成功后只生成：
% simulink_new/sim/stereo_ibvs_sim_stage1.slx

if nargin < 1
    projectDir = '';
end

%% 1. 查找core和config
[projectDir, coreFile, configFile, simDir] = ...
    locateProjectFiles(projectDir);

coreDir = fileparts(coreFile);
configDir = fileparts(configFile);

addpath(coreDir);
addpath(configDir);

fprintf('\n核心模型：%s\n', coreFile);
fprintf('配置文件：%s\n', configFile);
fprintf('仿真输出：%s\n', simDir);

%% 2. 读取配置，不修改配置文件
run(configFile);

if evalin('base', 'exist(''cfg'',''var'')') ~= 1
    error('StereoIBVSSim:CfgMissing', ...
        '运行配置文件后，基础工作区中没有生成cfg。');
end

cfgLocal = evalin('base', 'cfg');

validateConfiguration(cfgLocal);

% 仅在当前离线仿真工作区中临时开放Stage 1。
% 不修改stereo_ibvs_config.m。
cfgLocal.stage1CalibrationReady = true;
assignin('base', 'cfg', cfgLocal);

%% 3. 加载正确的core模型
[~, coreModelName, ~] = fileparts(coreFile);

if bdIsLoaded(coreModelName)
    loadedCoreFile = get_param(coreModelName, 'FileName');

    if ~samePath(loadedCoreFile, coreFile)
        close_system(coreModelName, 0);
    end
end

load_system(coreFile);

loadedCoreFile = get_param(coreModelName, 'FileName');

if ~samePath(loadedCoreFile, coreFile)
    error('StereoIBVSSim:WrongCoreLoaded', ...
        ['当前加载的core不是目标文件。\n' ...
         '实际：%s\n目标：%s'], ...
        loadedCoreFile, coreFile);
end

% 只做编译检查，不修改、不保存core模型。
set_param(coreModelName, 'SimulationCommand', 'update');

% core自己的InitFcn可能重新运行配置，因此更新后再次设置离线许可。
cfgLocal = evalin('base', 'cfg');
cfgLocal.stage1CalibrationReady = true;
assignin('base', 'cfg', cfgLocal);

%% 4. 新建sim模型
simModelName = 'stereo_ibvs_sim_stage1';
simModelFile = fullfile(simDir, [simModelName '.slx']);

if bdIsLoaded(simModelName)
    close_system(simModelName, 0);
end

if isfile(simModelFile)
    delete(simModelFile);
end

new_system(simModelName);
open_system(simModelName);

try
    configureSimulationModel(simModelName);
    configureSimulationCallbacks( ...
        simModelName, configFile, coreDir, configDir);

    addDescription(simModelName);

    scenarioPath = addStage1Scenario(simModelName);

    coreBlockPath = addCoreReference( ...
        simModelName, coreModelName);

    addOutputBlocks(simModelName);

    connectTopLevel( ...
        simModelName, scenarioPath, coreBlockPath);

    % 编译检查新生成的sim模型。
    set_param(simModelName, ...
        'SimulationCommand', 'update');

    save_system(simModelName, simModelFile);

    open_system(simModelName);
    set_param(simModelName, ...
        'ZoomFactor', 'FitSystem');

catch ME
    try
        save_system(simModelName, simModelFile);
    catch
    end

    fprintf(2, '\n生成sim模型失败：\n%s\n', ...
        getReport(ME, 'extended', ...
        'hyperlinks', 'off'));

    rethrow(ME);
end

fprintf('\n生成成功：\n%s\n', simModelFile);
fprintf('\n运行方式：\n');
fprintf('sim(''%s'');\n', simModelName);
fprintf('\n测试流程：\n');
fprintf('0~0.5秒：控制器关闭，关节速度为0。\n');
fprintf('0.5~3秒：目标偏离中心，产生非零关节速度。\n');
fprintf('3~4.5秒：目标回到中心，关节速度回到0附近。\n');
fprintf('4.5~6秒：视觉无效，关节速度持续为0。\n');

end


