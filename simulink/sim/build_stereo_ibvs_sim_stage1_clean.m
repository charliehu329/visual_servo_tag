    'addpath(''%s'');' ...
    'addpath(''%s'');' ...
    'run(''%s'');' ...
    'cfg.stage1CalibrationReady=true;' ...
    'assignin(''base'',''cfg'',cfg);'], ...
    coreDirEscaped, ...
    configDirEscaped, ...
    configEscaped);

