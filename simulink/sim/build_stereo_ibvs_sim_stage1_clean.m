function validateConfiguration(cfgLocal)
% 代码作用：
% 检查Stage 1仿真需要的配置参数和机器人模型。
%
% 输入参数含义：
% cfgLocal：
% stereo_ibvs_config生成的配置结构体。
%
% 输出参数含义：
% 本函数没有输出参数。

requiredFields = {
    'robot'
    'Ts'
    'qInitial'
    'zoomInitialSteps'
    'cameraIntrinsicsStage1'
    'cameraBodyName'
};

for fieldIndex = 1:numel(requiredFields)
    if ~isfield(cfgLocal, ...
            requiredFields{fieldIndex})
        error('StereoIBVSSim:MissingCfgField', ...
            'cfg缺少字段：%s', ...
            requiredFields{fieldIndex});
    end
end

if numel(homeConfiguration( ...
        cfgLocal.robot)) ~= 7
    error('StereoIBVSSim:RobotDOF', ...
        'cfg.robot必须是7自由度。');
end

if ~any(strcmp( ...
        cfgLocal.robot.BodyNames, ...
        cfgLocal.cameraBodyName))
    error('StereoIBVSSim:CameraBodyMissing', ...
        'cfg.robot中不存在相机刚体%s。', ...
        cfgLocal.cameraBodyName);
end

Jtest = geometricJacobian( ...
    cfgLocal.robot, ...
    reshape(cfgLocal.qInitial, 7, 1), ...
    cfgLocal.cameraBodyName);

if ~isequal(size(Jtest), [6 7])
    error('StereoIBVSSim:JacobianSize', ...
        '测试雅可比尺寸不是6×7。');
end
end


