function [projectDir, coreFile, ...
    configFile, simDir] = locateProjectFiles(projectDir)
% 代码作用：
% 自动查找core模型和配置文件。
%
% 输出参数含义：
% projectDir：
% 工程根目录。
%
% coreFile：
% stereo_ibvs_core.slx完整路径。
%
% configFile：
% stereo_ibvs_config.m完整路径。
%
% simDir：
% sim模型输出目录。

scriptDir = fileparts(mfilename('fullpath'));

candidateRoots = {};

if ~isempty(projectDir)
    candidateRoots{end+1} = char(projectDir);
end

candidateRoots = [candidateRoots, {
    scriptDir
    fileparts(scriptDir)
    pwd
    fileparts(pwd)
}];

projectDir = '';
coreFile = '';
configFile = '';

for rootIndex = 1:numel(candidateRoots)
    root = candidateRoots{rootIndex};

    if isempty(root) || ~isfolder(root)
        continue;
    end

    structuredCore = fullfile( ...
        root, 'core', ...
        'stereo_ibvs_core.slx');

    structuredConfig = fullfile( ...
        root, 'config', ...
        'stereo_ibvs_config.m');

    flatCore = fullfile( ...
        root, 'stereo_ibvs_core.slx');

    flatConfig = fullfile( ...
        root, 'stereo_ibvs_config.m');

    if isfile(structuredCore) && ...
            isfile(structuredConfig)
        projectDir = root;
        coreFile = structuredCore;
        configFile = structuredConfig;
        break;
    end

    if isfile(flatCore) && ...
            isfile(flatConfig)
        projectDir = root;
        coreFile = flatCore;
        configFile = flatConfig;
        break;
    end
end

if isempty(projectDir)
    error('StereoIBVSSim:ProjectNotFound', ...
        ['没有找到stereo_ibvs_core.slx和' ...
         'stereo_ibvs_config.m。']);
end

simDir = fullfile(projectDir, 'sim');

if ~isfolder(simDir)
    mkdir(simDir);
end
