function validateConfiguration(cfgLocal)
% 代码作用：
% 检查Stage 1离线仿真需要的配置和机器人模型。

requiredFields = {
    'robot'
    'Ts'
    'qInitial'
    'zoomInitialSteps'
    'cameraIntrinsicsStage1'
    'cameraBodyName'
    'robotBaseName'
    'stage1CalibrationReady'
};

for i = 1:numel(requiredFields)
    if ~isfield(cfgLocal,requiredFields{i})
        error('StereoIBVSSim:CfgFieldMissing', ...
            'cfg缺少字段：%s',requiredFields{i});
    end
end

if numel(homeConfiguration(cfgLocal.robot)) ~= 7
    error('StereoIBVSSim:RobotDOF', ...
        'cfg.robot不是7自由度。');
end

if ~any(strcmp( ...
        cfgLocal.robot.BodyNames, ...
        cfgLocal.cameraBodyName))
    error('StereoIBVSSim:CameraBody', ...
        'cfg.robot中不存在%s。', ...
        cfgLocal.cameraBodyName);
end

Jtest = geometricJacobian( ...
    cfgLocal.robot, ...
    reshape(cfgLocal.qInitial,7,1), ...
    cfgLocal.cameraBodyName);

if ~isequal(size(Jtest),[6 7])
    error('StereoIBVSSim:JacobianSize', ...
        '测试Jacobian不是6×7。');
end

if ~logical(cfgLocal.stage1CalibrationReady)
    error('StereoIBVSSim:SimulationPermission', ...
        '离线仿真许可没有成功启用。');
end
end


function repairKinematicsBlocks(coreModelName,cfgLocal)
% 代码作用：
% 修正Get Jacobian和Get Transform使用的刚体名称。

jacobianBlock = [ ...
    coreModelName ...
    '/04_FR3_Camera_Kinematics/Get Jacobian'];

transformBlock = [ ...
    coreModelName ...
    '/04_FR3_Camera_Kinematics/Get Transform'];

setRequiredParameter( ...
    jacobianBlock,'RigidBodyTree','cfg.robot');

setRequiredParameter( ...
    jacobianBlock,'EndEffector', ...
    cfgLocal.cameraBodyName);

setOptionalParameter( ...
    jacobianBlock,'SimulateUsing', ...
    'Interpreted execution');

setRequiredParameter( ...
    transformBlock,'RigidBodyTree','cfg.robot');

setRequiredParameter( ...
    transformBlock,'SourceBody', ...
    cfgLocal.cameraBodyName);

setRequiredParameter( ...
    transformBlock,'TargetBody', ...
    cfgLocal.robotBaseName);

setOptionalParameter( ...
    transformBlock,'SimulateUsing', ...
    'Interpreted execution');

fprintf('Get Jacobian末端：%s\n', ...
    get_param(jacobianBlock,'EndEffector'));
end


function configureCallbacks( ...
    modelName,configFile,coreDir,configDir)
% 代码作用：
% 配置仿真模型初始化和清理回调。

escapedConfig = escapeQuotes(configFile);
escapedCoreDir = escapeQuotes(coreDir);
escapedConfigDir = escapeQuotes(configDir);

initCallback = sprintf([ ...
    'addpath(''%s'');' ...
    'addpath(''%s'');' ...
    'assignin(''base'',' ...
    '''STEREO_IBVS_SIMULATION_MODE'',true);' ...
    'run(''%s'');'], ...
    escapedCoreDir,escapedConfigDir,escapedConfig);

cleanupCallback = sprintf([ ...
    'assignin(''base'',' ...
    '''STEREO_IBVS_SIMULATION_MODE'',false);' ...
    'run(''%s'');'], ...
    escapedConfig);

set_param(modelName,'InitFcn',initCallback);
set_param(modelName,'StopFcn',cleanupCallback);
set_param(modelName,'CloseFcn',cleanupCallback);
end


function ensureSimulationOverride(configFile)
% 代码作用：
% 在主配置文件中加入默认关闭的离线仿真许可。

configText = fileread(configFile);
marker = '% STEREO_IBVS_SIM_OVERRIDE_BEGIN';

if contains(configText,marker)
    return;
end

backupFile = [configFile ...
    '.before_sim_override.bak'];

if ~isfile(backupFile)
    copyfile(configFile,backupFile);
end

overrideCode = sprintf([ ...
    '\n%%%% Stage 1离线仿真许可\n' ...
    '%% STEREO_IBVS_SIM_OVERRIDE_BEGIN\n' ...
    'cfg.simulationMode = false;\n' ...
    'if evalin(''base'',' ...
    '''exist(''''STEREO_IBVS_SIMULATION_MODE'''',' ...
    '''''var'''') == 1'')\n' ...
    '    cfg.simulationMode = logical(evalin(' ...
    '''base'',' ...
    '''STEREO_IBVS_SIMULATION_MODE''));\n' ...
    'end\n' ...
    'if cfg.simulationMode\n' ...
    '    cfg.stage1CalibrationReady = true;\n' ...
    'end\n' ...
    '%% STEREO_IBVS_SIM_OVERRIDE_END\n\n']);

insertMarker = '%% 写入MATLAB基础工作区';
insertLocation = strfind(configText,insertMarker);

if isempty(insertLocation)
    error('StereoIBVSSim:ConfigMarker', ...
        '配置文件中找不到“%% 写入MATLAB基础工作区”。');
end

insertIndex = insertLocation(1);

newText = [ ...
    configText(1:insertIndex-1) ...
    overrideCode ...
    configText(insertIndex:end)];

writeUtf8(configFile,newText);
end


function setRequiredParameter( ...
    blockPath,parameterName,parameterValue)
% 代码作用：
% 设置必须存在的模块参数。

if getSimulinkBlockHandle(blockPath) == -1
    error('StereoIBVSSim:MissingBlock', ...
        '找不到模块：%s',blockPath);
end

parameters = get_param(blockPath,'DialogParameters');

if ~isfield(parameters,parameterName)
    error('StereoIBVSSim:MissingParameter', ...
        '模块%s没有参数%s。', ...
        blockPath,parameterName);
end

set_param(blockPath,parameterName,parameterValue);
end


function setOptionalParameter( ...
    blockPath,parameterName,parameterValue)
