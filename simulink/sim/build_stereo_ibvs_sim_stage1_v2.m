function writeUtf8(filePath,textContent)
% 代码作用：
% 使用UTF-8编码写入文本。

fileId = fopen(filePath,'w','n','UTF-8');

if fileId < 0
    error('StereoIBVSSim:WriteFile', ...
        '无法写入文件：%s',filePath);
end

cleanupObject = onCleanup(@() fclose(fileId));
fwrite(fileId,textContent,'char');
end


function escaped = escapeQuotes(textValue)
% 代码作用：
% 转义MATLAB回调字符串中的单引号。

escaped = strrep(textValue,'''','''''');
end


function result = samePath(pathA,pathB)
% 代码作用：
% 判断两个路径是否指向同一个文件。

canonicalA = char( ...
    java.io.File(pathA).getCanonicalPath());

canonicalB = char( ...
    java.io.File(pathB).getCanonicalPath());

result = strcmp(canonicalA,canonicalB);
end


function [projectDir,coreFile, ...
    configFile,simDir] = locateProjectFiles(projectDir)
% 代码作用：
% 自动查找核心模型和配置文件。
%
% 输出参数含义：
% projectDir：
% 找到的工程根目录。
%
% coreFile：
% 核心模型完整路径。
%
% configFile：
% 配置文件完整路径。
%
% simDir：
% 仿真模型输出目录。

scriptDir = fileparts(mfilename('fullpath'));

candidateRoots = {};

if ~isempty(projectDir)
    candidateRoots{end+1} = char(projectDir);
end

candidateRoots = [candidateRoots, {
    scriptDir
    fileparts(scriptDir)
    pwd
    fileparts(pwd)
}];

projectDir = '';
coreFile = '';
configFile = '';

for i = 1:numel(candidateRoots)
    root = candidateRoots{i};

    if isempty(root) || ~isfolder(root)
        continue;
    end

    structuredCore = fullfile( ...
        root,'core','stereo_ibvs_core.slx');

    structuredConfig = fullfile( ...
        root,'config','stereo_ibvs_config.m');

    flatCore = fullfile( ...
        root,'stereo_ibvs_core.slx');

    flatConfig = fullfile( ...
        root,'stereo_ibvs_config.m');

    if isfile(structuredCore) && ...
            isfile(structuredConfig)
        projectDir = root;
        coreFile = structuredCore;
        configFile = structuredConfig;
        break;
    end

    if isfile(flatCore) && isfile(flatConfig)
        projectDir = root;
        coreFile = flatCore;
        configFile = flatConfig;
        break;
    end
end

if isempty(projectDir)
    error('StereoIBVSSim:ProjectNotFound', ...
        ['没有找到stereo_ibvs_core.slx和' ...
         'stereo_ibvs_config.m。']);
end

simDir = fullfile(projectDir,'sim');

if ~isfolder(simDir)
    mkdir(simDir);
end
end
