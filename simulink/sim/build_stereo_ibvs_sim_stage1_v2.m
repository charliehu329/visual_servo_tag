function build_stereo_ibvs_sim_stage1_v2(projectDir)
% 代码作用：
% 自动生成Stage 1离线仿真模型stereo_ibvs_sim_stage1.slx。
% 该模型通过Model块引用现有stereo_ibvs_core.slx。
%
% 输入参数含义：
% projectDir：
% simulink_new目录。可以省略，脚本会自动查找。
%
% 输出参数含义：
% 本函数没有输出参数。
% 成功后会在sim目录生成stereo_ibvs_sim_stage1.slx。

if nargin < 1
    projectDir = '';
end

%% 1. 查找文件
[projectDir,coreFile,configFile,simDir] = ...
    locateProjectFiles(projectDir);

coreDir = fileparts(coreFile);
configDir = fileparts(configFile);

addpath(coreDir);
addpath(configDir);
addpath(simDir);

fprintf('\n核心模型：%s\n',coreFile);
fprintf('配置文件：%s\n',configFile);
fprintf('仿真目录：%s\n',simDir);

%% 2. 为配置文件加入离线仿真许可
ensureSimulationOverride(configFile);

assignin('base','STEREO_IBVS_SIMULATION_MODE',true);
run(configFile);

cfgLocal = evalin('base','cfg');
validateConfiguration(cfgLocal);

%% 3. 加载并检查正确的core模型
[~,coreModelName,~] = fileparts(coreFile);

if bdIsLoaded(coreModelName)
    loadedFile = get_param(coreModelName,'FileName');

    if ~samePath(loadedFile,coreFile)
        close_system(coreModelName,0);
    end
end

load_system(coreFile);

if ~samePath(get_param(coreModelName,'FileName'),coreFile)
    error('StereoIBVSSim:WrongCore', ...
        'MATLAB加载的不是指定的stereo_ibvs_core.slx。');
end

repairKinematicsBlocks(coreModelName,cfgLocal);
save_system(coreModelName);

set_param(coreModelName,'SimulationCommand','update');

%% 4. 创建仿真模型
simModelName = 'stereo_ibvs_sim_stage1';
simModelFile = fullfile(simDir,[simModelName '.slx']);

if bdIsLoaded(simModelName)
    close_system(simModelName,0);
end

if isfile(simModelFile)
    backupFile = fullfile(simDir, ...
        [simModelName '_backup_' ...
        char(datetime('now','Format','yyyyMMdd_HHmmss')) ...
        '.slx']);

    copyfile(simModelFile,backupFile);
    fprintf('旧模型已备份：%s\n',backupFile);
end

new_system(simModelName);
open_system(simModelName);

try
    set_param( ...
        simModelName, ...
        'StartTime','0', ...
        'StopTime','6', ...
        'SolverType','Fixed-step', ...
        'Solver','FixedStepDiscrete', ...
        'FixedStep','cfg.Ts', ...
        'ReturnWorkspaceOutputs','on');

    configureCallbacks(simModelName,configFile,coreDir,configDir);

    scenarioPath = buildScenario(simModelName);

    coreBlockPath = [simModelName '/Stereo IBVS Core'];

    add_block( ...
        'simulink/Ports & Subsystems/Model', ...
        coreBlockPath, ...
        'Position',[430 155 700 455]);

    set_param( ...
        coreBlockPath, ...
        'ModelName',coreModelName, ...
        'SimulationMode','Normal');

    try
        Simulink.BlockDiagram.refreshBlocks(simModelName);
    catch
        set_param(simModelName,'SimulationCommand','update');
    end

    coreHandles = get_param(coreBlockPath,'PortHandles');

    if numel(coreHandles.Inport) ~= 5 || ...
            numel(coreHandles.Outport) ~= 3
        error('StereoIBVSSim:CorePorts', ...
            ['core接口错误：实际输入%d个、输出%d个，' ...
             '应为输入5个、输出3个。'], ...
            numel(coreHandles.Inport), ...
            numel(coreHandles.Outport));
    end

    addOutputsAndLogging(simModelName);
    connectTopLevel(simModelName,scenarioPath,coreBlockPath);

    set_param(simModelName,'SimulationCommand','update');
    save_system(simModelName,simModelFile);

    open_system(simModelName);
    set_param(simModelName,'ZoomFactor','FitSystem');

catch ME
    try
        save_system(simModelName,simModelFile);
    catch
    end

    assignin('base','STEREO_IBVS_SIMULATION_MODE',false);

    fprintf(2,'\n构建失败：\n%s\n', ...
        getReport(ME,'extended','hyperlinks','off'));

    rethrow(ME);
end

fprintf('\n生成成功：%s\n',simModelFile);
fprintf('\n测试过程：\n');
fprintf('0~0.5秒：控制器关闭，速度为0。\n');
fprintf('0.5~3秒：目标偏离中心，产生非零速度。\n');
fprintf('3~4.5秒：目标回到中心，速度回到0附近。\n');
fprintf('4.5~6秒：视觉无效，持续输出0速度。\n');
fprintf('\n运行：sim(''%s'')\n',simModelName);

end


